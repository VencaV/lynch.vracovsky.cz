  <h2 id="zal2">Recenze</h2>
  <p>
  David Lynch natočil nový film&nbsp;&mdash; tímto faktem by se dala celá recenze obsáhnout. Lidé, kteří Lynche můžou, budou
  ho po jeho novém počinu mít rádi stále; ty, kteří ho nemusí si tímto filmem nejspíš na svou stranu nezískal.
  Inland Empire je typickým Lynchovým filmem se spoustou tajemna, na první (i další) pohled nepochopitelných lidí 
  i situací, hojně odkazující na předešlá Lynchova díla (Lost Highway, Twin Peaks, Mulholland Drive). Ač je zážitek
  ze sálu kina ještě čerstvý a nechci se dopustit nějakého ukvapeného závěru; přesto musím Inland Empire zařadit
  k tomu absolutně nejlepšímu, co David Lynch kdy natočil.
  </p>
  <p>
  K natočení filmu použil Lynch digitální kameru a hlavně v prvních chvílích to divák vnímá&nbsp;&mdash; zvláště záběry 
  točené bez stativu, ale po chvíli si oko navykne. Kvůli digitální kameře je samozřejmě oproti předchozím "Technicolorovým" 
  filmům viditelný rozdíl, ale v tomto případě si myslím, že ku prospěchu věci. Nemyslím, že by Lynch svoji 
  fascinaci barvami obětoval jen díky levnému materiálu, ale rozhodně to byl záměr, spolu s dalšími detaily
  tvořící jedinečný celek.
  </p>
  <p>
  Dalším "detailem" zapadajícím do této velkolepé mozaiky je herecké obsazení. Jeremy Irons i Justin Theroux
  jsou skvělí; svoji malou, ale nepřehlédnutelnou roli si zde střihla Lynchova stálice Grace Zabriskie.
  Další z Lynchových herců, Harry Dean Stanton coby svérázný asistent režie je jako vždy úžasný,
  i Polští herci jsou přesvědčiví a jaksi "východně tajemní".
  A nakonec Laura Dern. Nedovedu si představit moc hereček v této roli, kdy v podstatě celé tři hodiny
  "nesleze z plátna", navíc jí Lynch dopřál dost a dost dlouhých detailních záběrů na obličej. Od dob Modrého Sametu
  a Zběsilosti v srdci nejenže zestárla (bez tohoto podstatného faktu by si tuto roli nezahrála), ale hlavně
  vyzrála v herečku, jejíž obličej ty tři hodiny detailů a polodetailů unese. Unese naprosto bez problémů,
  stejně jako Laura Dern bez problémů vypouští z úst jedno sprosté slovo za druhým, aniž by ztratila grácii.
  Už pro tento výkon stojí za to Inland Empire vidět.
  </p>
  <p>
  I přes vynikající výkon Laury Dern je něco, co jí v tomto filmu převyšuje. Tím něčím je <strong>atmosféra</strong>.
  Amtosféra, která je velmi důležitá (a povětšinou výtečná) ve všech Lynchových počinech, by se v Inland Empire
  dala vskutku krájet. Dlouhé temné chodby, temné pokoje s ještě temnějším osazenstvem, setmělé zasněžené ulice 
  Polského města, špinavá tmavá schodiště, plačící žena před televizí, neposatelné hluboké zvuky&hellip; Někomu se to 
  může zdát jako klišé, navíc samotným režisérem mnohokrát použité&nbsp;&mdash; ale funguje. Opravdu funguje.
  Film s temnější atmosférou aby jeden pohledal, snad Sedmá pečeť nebo&hellip; nebo ranný Lynch (Grandmother, 
  Eraserhead).
  </p>
  <p>
  O ději se moc nemá cenu zde rozepisovat, jak již Lynchovi fanoušci zaregistrovali, tak děj u většiny jeho filmů 
  nebývá jednoznačný a jednoznačně vyložitelný a pochopitelný. Snad jen to, že Laura Dern zemře a nezemře, bude svému 
  manželovi věrná a nevěrná&hellip; ale v každém případě zmatená ("Nevím co se stalo dřív, až mi z toho jebe v hlavě.").
  Dále třeba to, že se ve filmu objeví králičí rodinka známá z Lynchova prazvláštního seriálu <a href="f23.php" 
  title="Rabbits">Rabbits</a>. Znalý divák také zaregistruje mnoho odkazů na předchozí Lynchovy filmy (případně
  seriály), hlavně Mulholland Drive, Lost Highway a Twin Peaks.
  </p>
  <p>
  Inland Empire je skvělý film. I na Lynchovy poměry zvláštní, nejen kvůli použité digitální kameře, možná trochu
  hůře stravitelný, ale přesto skvělý. Podívejte se na něj v klidu a nechte ho na sebe působit, to je myslím to 
  nejlepší doporučení, které vám mohu prostřednictvím této recenze dát. Ať se řadíte mezi Lynchovy fanoušky či 
  odpůrce, jedno je jisté&nbsp;&mdash; Inland Empire na vás zapůsobí.
  </p>
