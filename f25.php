  Pár postřehů:
  </p>
  <ol style="list-style-type:lower-alpha">
  <li>
  <strong>Prostitutka ze začátku = hnědovlasá dívka (lost girl).</strong> 
  Je tam střih, kdy "prostitutka se zamlženou tváří" sedí polonahá 
  na kraji postele a vzápětí vidíme, že polonahá sedí na kraji postele ta hnědovlasá dívka sledující "televizor". To podle té 
  mé teorie znamená, že hnědovláska po sexu upadla do vigilního kómatu a její mysl je abstrakčně uvězněna v "pokoji s televizí".
  </li>
  <li>
  <strong>Skroro celý film je iluzí hnědovlásky (lost girl) v kómatu.</strong> 
  V té "televizi" sledujeme, ta hnědovláska sleduje, zrychlené 
  záběry sitkomu a příchod šílené sousedky za herečkou Nikki. Pokud je "pokoj s televizí" pouze abstrakčním zobrazením uvězněné 
  mysli (vigilní kóma) té hnědovlasé ženy, potom by už v normální rychlosti zobrazené záběry sitkomu, příchod sousedky a její 
  následný rozhovor s Nikki plus další s tím logicky související záběry, měly být výplodem této uvězněné mysli. Stejně jako sen 
  je iluzí vycházející z reality, tak i to, co by prožívala ta hnědovláska v kómatu, by mělo nějaký reálný podklad. Takže herečka 
  Nikki může být skutečnou herečkou, kterou ta dívka někdy předtím sledovala ve skutečné televizi, ale ten příběh Nikki z 
  natáčení by byl její iluzí.
  </li>
  <li>
  <strong>Upadnutí do kómatu a probrání se z něho.</strong>
  Ten začátek na mě působí, jakoby té hnědovlásce odcházela vědomá mysl přes předvědomí (pokoj č.47 s králíky) do nevědomí a 
  konec, jakoby se její vědomá mysl navracela zpátky z nevědomí (chodby před pokojem č.47 strážené démonem) a předvědomí (pokoj 
  č.47). V hotelovém pokoji po upadnutí do kóma je polonahá, doma po "probuzení" je už samozřejmě oblečená. To, že běží z 
  hotelového pokoje po krásné chodbě, potom z ničehonic po škaredých schodech a najednou je doma, podle mě znázorňuje ten její 
  návrat do vědomé mysli (abstrakce). Uvědomění si toho, že naposledy byla při vědomí v hotelovém pokoji a teď je doma. 
  Přirovnal bych to k probuzení doma po "oknu" z alkoholu.
  </li>
  <li>
  <strong>"Axxon n" by mohlo znamenat po vyrušení jednoho "x" a jednoho "n" "axon", což je v angličtině "nervový výběžek".</strong>
  </ol>
  <p>
  Myslím si, že "lost girl" chtěla sdílet budoucí život a vychovávat budoucí dítě s milencem, ve kterém se spletla a kvůli 
  kterému upadla do kómatu při jejich prvním sexu, kde se nečekaně projevil jako sadista. Myslím si, že se to stalo v nějakém 
  hotelovém pokoji a také, že sex s ním vnímala jako nevěru hlavně proto, že ještě pořád byla vdaná a byla věřící (její 
  modlení v jednom záběru). Také si myslím, že ještě pořád svého muže měla ráda, ale ten ji prostě nemohl dát dítě, protože byl 
  neplodný, k čemuž se ji přiznal později po svatbě. Nevíme, jak v kómatu přesně probíhal její život... Asi ji někdo v tom 
  stavu objevil v hotelu, převezli ji do nemocnice, později porodila syna a po určité době si ji manžel odvezl domů i s 
  chlapcem, kterého vychovával. Na konci filmu se doma z kómatu probrala a běžela uvítat navracejícího se manžela. Přitom 
  zjistila, že má syna.
  </p>
