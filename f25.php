  <h2 id="zal3">Možné vysvětlení</h2>
  <p>
  Jednoznačně pochopit film dle mého mínění nelze, jelikož chybí jednoznačná vodítka. Kde je realita? Komu se to celé odehrává 
  v hlavě? <strong>První možnost:</strong> skoro celý film je fantazie té mladé hnědovlasé manželky, která sedí na kraji 
  postele, brečí, dívá se na "televizi" a přitom v hlavě běží její "film". <strong>Druhá možnost:</strong> Skoro celý film je 
  vědomá fantazie té šílené sousedky při návštěvě herečky Nikki. <strong>Třetí možnost:</strong> herečka Nikki se tak oddala 
  své filmové roli, že ztratila pojem o tom, co je realita a filmová fikce.
  </p>
  <p>
  Já se přikláním k první možnosti a to proto, že na začátku filmu je zobrazen její "sex s klientem", který je ale podle mě ve 
  skutečnosti jen její nevěrou s cílem otěhotnět, protože manžel nemůže mít děti. Potom je prostřih, že vlastně po tom sexu 
  sedí "doma" na posteli, brečí a kouká na "televizi", kde zrychleně běží sitcom s králíky a návštěva šílené sousedky u Nikki. 
  To, že ale televize není skutečnou televizí, je ve filmu několikrát naznačeno. Jakoby tím "pokojem s televizí" Lynch 
  naznačoval, že jsme v mysli té ženy, v jejím nevědomí, ...že je v kómatu. Myslím tím stav sníženého vědomí, kdy je porucha 
  vědomí na stupni kómatu, tzn. že člověk je ve stavu vigilního kómatu (bdělé kóma)- tedy má otevřené oči, zachované reflexy, 
  ovšem nereaguje vůbec na jakékoliv podněty (štípnutí, zatřesení, oslovení). Takže od začátku sitcomu až skoro do konce filmu, 
  kde tato hnědovlasá žena uslyší přicházet manžela se synem, sledujeme její fantazii v kómatu, do kterého se dostala při tom 
  (asi brutálním) sexu a při kterém otěhotněla. Konec filmu asi naznačuje její probrání se z kómatu. Syn už je veliký a její 
  manžel se s jeho nevlastním a jejím vlastním synem za ní vrací domů. Laura Dernová pak byla její projekcí a naší průvodkyní v 
  té její původně uvězněné mysli.
  </p>
  <p>
  Konkrétně: hnědovláska (lost girl) se zamilovala a vdala za muže s polskými kořeny. Mají dům č. 1358 někde v Americe a chtěla 
  by s ním otěhotnět, což se pořád nedaří. Ve lsti mu řekne, že je těhotná a on se přizná, že nemůže mít děti, protože v polském 
  městě, kde vyrůstal, byla chemická továrna, která vypouštěla něco do ovzduší. Její mateřský pud je tak silný, že se rozhodne 
  otěhotnět s jiným mužem, možná s ním sdílet i budoucí život, kterého poznala v baru. Je to sympatický mariňák ze Severní 
  Karolíny. Poslední věc, kterou si před upadnutím do vigilního kómatu "mlhavě" pamatuje (proto ty "zamlžené" obličeje na začátku 
  a řeči: "To je ten pokoj?", "Nepoznávám ho." -> Byl to nějaký pokoj, ale tak jak ho vidíme, přesně nevypadal), je příchod s 
  mariňákem do hotelového pokoje. Tohoto muže ale neodhadla správně a proto jejich následný sex je spíš jejím "znásilněním", za 
  který by mu "nejraději vypíchla oko a rozmačkala koule". Potom upadne do kómatu (neustálé prostřihy na její obličej v celém 
  filmu -> je to "příběh" této ženy), kde v její "myšlenkové fantazii", kromě jiného, sledujeme i její skutečné pocity, kde si 
  připadá (připadala) jako děvka, protože musela být nevěrná. Možná sledujeme i její porodní bolesti ("bolesti po zapíchnutí 
  šroubováku do břicha", šroubovák v podbříšku -> penis v pochvě) atd. Na konci filmu se z tohoto bdělého kómatu probere, uvidí, 
  že je v domě svého manžela, běží ho přivítat a s ním i svého syna, kterého porodila v nevědomí.
  </p>
