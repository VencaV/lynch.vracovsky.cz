  <h2 id="zal2">Recenze</h2>
  <p>
  Ve svém celovečerním černobílém debutu z roku 1977 vás režisér David Lynch uvrhne do podivně svíravého technokratického světa, 
  kde o pocity klaustrofobie a poruchy trávicí soustavy nebude nouze. Realita je skryta pod tíživým nánosem černočerné temnoty, 
  která dává prostor k odhalení nejtemnějších propastí lidské mysli.
  </p>
  <p>
  <img class="right" src="img/f04/02.jpg" alt="Eraserhead" height="156" width="270">
  Dá-li se v případě Mazací hlavy vůbec mluvit o nějakém příběhu (v klasickém slova smyslu), interpretací je nesčetně mnoho. 
  Někteří ve snímku nacházejí složitý propletenec ďábelské symboliky, jiní v něm vidí "jen" dokonale promyšlený sled 
  bizarně-fantaskních obrazů, další zase soubor několika nepochopitelných experimentálních scén sloužící jen k "vymazání" mozku. 
  Je na každém jedinci, aby se s dějem filmu vypořádal po svém.
  </p>
  <p>
  Obrazové zpracování do jisté míry evokuje staré němé filmy. Kamera si libuje ve hře světla a stínu, v dlouhých statických 
  záběrech. Herci jsou jako v transu. Za celých devadesát minut trvání filmu padne ve snímku jen něco málo přes sto vět. Velice 
  důležitým aspektem je zvukový "doprovod". Uvozovky jsou v tomto případě na místě. Industriální ruchy, kňourání, neurotické 
  skřeky, šumy a hlukové stěny tvoří svébytnou složku filmu a provázejí jej po celou dobu. Ke gradaci děje a pocuchání nervů 
  nemohlo být využito lepších prostředků.
  </p>
  <p>
  <img class="right" src="img/f04/03.jpg" alt="Eraserhead" height="187" width="250">
  Lynch moc dobře ví, jak si pohrát s divákovým podvědomím. Mazací hlava ve vás odhalí nedefinovatelný pocit, který byl uložen 
  někde hluboko ve vašem nitru. Teď se dere na povrch. Záleží na individuální osobnosti každého z nás, jak se s ním popere. Ať 
  už vám Mazací hlava navodí pocit zvracení, deprese, znechucení, nudy, zvrácené harmonie či čehokoliv jiného, určitě vás 
  zasáhne. A to bez ohledu na to, zda se vám film bude líbit či nikoliv.
  </p>
