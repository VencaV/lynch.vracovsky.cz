  <h2 id="zal1">Obsah</h2>
  <p>
  Hrdinou depresivního surrealistického hororu je tiskař Henry Spencer (Jack Nance), který se nečekaně stává 
  zoufale rozpačitým opatrovníkem zrůdného novorozence své lhostejné přítelkyně Mary. V jediném dni Henry 
  zažije prazvláštní dobrodružství: jeho hlava je zpracována v továrně na tužky a on sám zbloudí v labyrintu 
  vlastní duše. Sled hypnotických obrazů se dnes obvykle interpretuje jako autobiograficky laděná vize autora, 
  který se obtížně vyrovnával s rolí manžela a novopečeného otce dcery Jennifer, jež se narodila s tělesnou 
  vadou. Lynch nikdy neprozradil, z čeho a jak udělal model monstra, které tak trápí hlavního hrdinu.</p>
