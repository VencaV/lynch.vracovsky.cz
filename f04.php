  <h2 id="zal3">Z diskuzí...</h2>
  <ul>
  <li>Eraserhead je hlubokou sondou do nejtemnějších propastí lidské mysli, kde realistický pohled nevidí přes veškerou černotu 
  nic. Inspirace v nejděsivějších nočních můrách předvídá jedinečnou atmosféru, která je rozvíjena brilantním zpracováním 
  technické stránky filmu. Prostředí je klaustrofobické, industriálně-gotické, technokratické a nepříjemné ve své všednosti a 
  zdánlivé obyčejnosti. Kamera je povětšinou statická a umírněný střih způsobuje velmi dlouhé záběry. Náměsíčné projevy herců a 
  všudypřítomná čerň přidávají na dojmu mlžné clony mezi divákem a obrazem. Jak i v pozdějších dílech David Lynch dokáže, je 
  mistrem v práci se světlem a stínem, což v černobílém filmu vyniká dvojnásob. Záběry jsou díky tomu dokonale zkomponované. 
  V tom vidím podobu s obrazovým způsobem vyjadřování němého filmu. Tomu ovšem odporuje použití zvuku jako jednoho z 
  nejdůležitějších výrazových prostředků. Jednou z nejdůležitějších složek tohoto filmu je totiž právě zvuk, který zde často 
  působí spíše jako hluk. Nepříjemné pazvuky a skřeky místy provázejí celý děj a gradují v depresi diváka jako celá ta děsivá 
  atmosféra. Ta je navozena okamžitě, od samého začátku, a její vliv je pomalu rozvíjen po celou dobu. Vím, že pro Lynche je 
  otázka na smysl filmu vedlejší. Neměl bych, ale nemohu se ubránit snaze hledat ve filmu víc než děs a pokus uvrhnout diváky 
  do stejné sžíravé deprese, v jaké je laděn celý tento film. Dělá na mě dojem, že je poskládán ze zcela náhodných vjemů, což je 
  možná způsobeno mnoha nelogickými odchylkami od narativní linie ve prospěch bizarních představ (tím myslím například scény 
  uvnitř radiátoru). Opět se nabízejí stovky a stovky otázek. Co znamenal muž s podivně znetvořenou tváří na hliněné planetě? 
  Snad ďábla, co pákami řídí lidské životy a tvoří ona záhadná malá monstra? Je vize světa, jakožto prázdného a bezútěšného 
  místa jen pouhý náhled do temných snů Davida Lynche nebo skrývaná pravda? Co mohl představovat obraz na zdi, zobrazující 
  výbuch atomové bomby? Ten cynismus a absurdní změnu evokuje už Lynchův oblíbený, obavy vzbuzující motiv pohasínajících 
  světel (jen v tomto filmu byl použit alespoň čtyřikrát). Věci teprve v průběhu filmu pod tíhou souvislostí nabývají svých 
  symbolických významů. Souhlasím s často prohlašovanou teorií, že poselstvím filmu Eraserhead je prosté vymazání, jak nám říká 
  už název. Zánik - možná utkvělých skutečností a axiomů o okolním světě a nástup šílených představ na vyšší úroveň. Samotné 
  vymazání je později demonstrováno motivem vířícího prachu z gumovátek na tužky. Eraserhead je rozhodně mistrovský počin, ale 
  jeho ponurá atmosféra na mě byla možná trochu příliš. Lynch se vůbec neobtěžoval budit zdání nějaké reálnosti jak jsem byl 
