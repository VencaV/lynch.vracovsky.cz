  hlavě.]</i> Alice a Pete se pomilují na zemi. Žena však v nejlepším Petea odstrčí a odejde do chaty. <i>[Skutečnost, 
  že Renée už Freda nemilovala, se odráží ve snu.]</i> 
  </p>
  <p>
  Pete vstane ze země jako Fred. <i>[Pete ze snu je Fred.]</i> Ten objeví v chatě jen tajemného muže, tvrdícího, že 
  Alice odešla a že je to ve skutečnosti jeho žena Renée. Tajemný muž pak s videokamerou pronásleduje 
