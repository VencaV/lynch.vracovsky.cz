  <h2 id="zal1">Obsah</h2>
  <p>
  Žárlivý jazzový hudebník Madison(Bill Pullman) se ocitne ve vězení za vraždu své manželky Renée (Patricia 
  Arquette). Tvrdí však, že je nevinný. Dozorci místo něj jednoho dne v cele najdou docela jiného muže – 
  automechanika Petea (Balthazar Getty). Ten se po propuštění zaplete s gangsterskou milenkou Alicí	(opět 
  Patricia Arquette),což (opět) nemůže skončit jinak než tragédií... V rafinovaně komponované šarádě Lynch 
  dovádí do důsledků svůj názor, že film nemusí svět reflektovat jednoznačným, realistickým způsobem: 
  vyprávění koncipované jako „psychogenní fuga“ pravděpodobně líčí hrdinovu snahu vyhnout se důsledkům svého 
  zločinu prostřednictvím úniku do vysněné identity.
