  <h3>ad 1)</h3>
  <p>
  Schválně si všimněte, jak funguje snová logika. Ve filmu jsou dvě scény s čísly 25 a 26. Jednou jde Pete 
  po schodech v Andyho domě nahoru, schodiště se změní, oslňující světla, Peteovi teče krev z nosu, Pete mine 
  pokoj č. 25 a jde do pokoje č.26, vidí divně, ale uvnitř je Alice (evidentně se miluje) a říká mu "Chtěl jsi 
  se mnou mluvit, he? Chtěl jsi se mě zeptat proč?" Když dveře zavře, chodba se změní, světla zmizí. Tato část 
  filmu je snem Freda ve vězení. Že Fred spí, víme i z toho, že vězeňský doktor mu dal sedativa a řekl "Teď 
