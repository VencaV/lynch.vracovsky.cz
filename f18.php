  <h2 id="zal2">Recenze</h2>
  <p>
  Kriminální thriller Lost Highway, který natočil kontroverzní režisér David Lynch podle scénáře Barryho Gifforda 
  (autora románu Zběsilost v srdci), tvůrci označují za „hororový film noir 21. století, přeplněný zoufalými 
  muži, nevěrnými ženami, přepychovými auty a levnými motely“. Bizarně zacyklený, jakoby „pozpátku“ puštěný 
  příběh vypráví o jazzovém saxofonistovi (Bill Pullman), jenž objeví videokazetu s bestiální vraždou své krásné 
  tmavovlasé ženy. Vzápětí je z brutálního mordu obviněn, v cele smrti však „vstoupí“ do těla mladého 
  automechanika (Balthazar Getty), jehož okouzlí smyslná blonďatá milenka gangsterského bosse, nápadně se 
  podobající zavražděné oběti (obě „femmes fatales“ hraje Patricia Arquetteová).</p>
  <p>Snímek jako obvykle kombinuje věci hrůzostrašné a banální: režisérův zběsilý svět ohně, krve rudé jako 
  rtěnka z bordelu, perverzních vášní, nočních můr, sadistických zločinů a schizoidních metamorfóz je 
  prostoupen řadou filmových citací. Lynch tentokrát skládá několikanásobný hold Alfredu Hitchcockovi 
  (psychické transformace jsou spojeny s fyzickou proměnou jako u Perkinsova majitele motelu v Psychu, 
  krásné ženy se jako Kim Novaková ve Vertigu vracejí z říše mrtvých s různými barvami vlasů i nehtů i s 
  odlišnou povahou).</p>
  <img class="right" src="img/f18/02.jpg" alt="Bill Pullman a Patricia Arquette" height="223" width="320">
  <p>Lost Highway je však filmem pozoruhodným pouze v jednotlivostech: především díky drásavé zvukové stopě a 
  skvělému soundtracku. Celkově působí – i se všemi makrodetaily rtů, zrnícími obrazovkami, přeostřovanými 
  záběry a tónováním do barev noci či reklamy – jako chladně odtažité stylistické „noir“ cvičení.</p>
  <p>Podobně jako předchozí Lynchovy filmy, i Lost Highway vyvolává řadu znepokojivých otázek, tentokrát 
  ale poskytuje pramálo uspokojivých odpovědí. Surreálné mystérium o vraždě, sexu a ztracené identitě připomíná 
  zmatenou křížovku, jejíž „dvojakou“ tajenku determinuje jekyll-hydeovské rozštěpení většiny charakterů a která 
  jen variuje motivy z filmů Modrý samet, Zběsilost v srdci a Twin Peaks. I ty se sice řídily logikou snu, 
  nikoli rozumu, Lost Highway však nedosahuje ani jejich dráždivé uhrančivosti, ani psychoanalytických přesahů. 
  Lynch publikum zahrnuje matoucími klíči, které by měly pomoci odemknout zámky od tajné třinácté komnaty lidské 
  psýchy, místo toho se ale při prvním použití lámou. Zatímco zmíněné filmy zobrazovaly reálný svět na pokraji 
  chaosu, Lost Highway působí, jako by se i Lynch ve zmateném provozu své dálnice ztratil.</p>
