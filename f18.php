  jejího bohatého známého, Andyho, jehož poznala v podniku Moke´s. Budou mít peníze na útěk před Eddym. Mladík 
  donutí milenku prozradit, že ji Andy přiměl natáčet pro mafiána porno. Pochopí, že se jí to líbilo, přestože 
  k tomu byla donucena. <i>[Sen vychází z reality.]</i> Sheila ztropí Peteovi scénu a opustí ho. Mladíkovi zavolá domů 
  pan Eddy, který pak předá sluchátko tajemnému muži. Ten Petea k smrti vyděsí vyprávěním o popravách na Dálném 
  východě. <i>[Fredovo schizofrenické já, které ho i ve skutečnosti děsí, se objevuje ve snu.]</i> Podle Alicina plánu 
  Pete omráčí Andyho v jeho domě. Oběť se však probere a zaútočí. Po Peteově zásahu však narazí do skleněné 
  stolní desky, která mu pronikne do mozku. Alice sebere cennosti. Na promítacím plátně běží celou dobu její 
