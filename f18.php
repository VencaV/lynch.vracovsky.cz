  <h2 id="zal6">Možné vysvětlení - další příklady</h2>
  <p>
  Na objasnění Fredova pocitu při neúspěšném milování s Renée v baráku tu máme taky jeho sen ve vězení. Zdálo 
  se mu, že se Pete (Fred) miluje s Alice (Renée) v poušti a ona mu říká: "Nikdy mě nebudeš mít.", což 
  přeneseně znamená "Nikdy tě nebudu skutečně milovat". Takže Fredovi se to milování nepovedlo, protože při 
  tom cítil, že ho Renée nemiluje, že dostává pouze její tělo a ne kvůli tomu, že by nemohl.
  </p>
  <p>
  Fred jde hrát do klubu a když zavolá domů, tak zjistí, že manželka není doma. Jeho žárlivost dosáhne stupně, 
  kdy se u něho rozjede schizofrenie. To je znázorněno "první kazetou". Něco se k nim dobývá do domu. Je to 
  dvojsmysl. Dům zároveň představuje Fredovu hlavu. Do Fredovy hlavy se snaží dostat druhé ego. Tajemný muž na 
  večírku: "Pozval jsi mě. Nemám ve zvyku chodit tam, kde nejsem pozvaný." -> "Vyvolal jsi mě svoji 
  žárlivostí." Když se potom další noc Fred pomiluje s Renée, milování se mu nepovede, protože cítí, že ho 
  manželka nemiluje, schizofrenie se rozjíždí definitivně. Místo obličeje Renée uvidí obličej tajemného muže 
  (druhého já). Tady už Fredův mozek stvořil přímo podobu druhého ega. A po tom, co manželé zalehnou, se Fred 
  začne procházet po domě jako ego tajemný muž. To je znázorněno "druhou kazetou". Takže ty scény s těmi 
  "kazetami" jsou takovým výrazovým prostředkem Lynche k zobrazení vzniku a postupující schizofrenie u Freda a 
  ty "kazety", lépe řečeno ten "záznam na nich", představují vzpomínky Freda přepnutého na ego tajemný muž. 
  Jakoby se Fred u výslechu snažil lovit vzpomínky druhého ega (přehrával si je v hlavě) a tím zjistil, co v 
  těch jednotlivých nocích dělal. Když se neúspěšně pomiloval s Renée, tak potom ještě chodil po domě jako 
  tajemný muž a když přijel z večírku, tak manželku jako ego tajemný muž zabil. Je to logické. Když ti 
  policajti přišli k Madisonovým domů, tak tam přišli kvůli kazetám, ale ty kazety nemohou být skutečné, 
  protože je natočil tajemný muž (jediná postava ve filmu, která měla kameru a točila), který taky není 
  skutečný. Mohl by je teoreticky natočit Fred. Je přece vlastně i tajemným mužem, i když o tom neví. Ale ani 
  tohle nejde, protože na druhé kazetě je natočený sám Fred s manželkou, když spí a ta kamera se pohybuje 
  (není na stativu). Takže když nejsou kazety skutečné, tak ve skutečnosti nemohou kvůli nim přijít policajti 
  do domu Madisonových a Fred s Renée je nemohou dostávat na zápraží. Musí to tedy být abstrakce.
