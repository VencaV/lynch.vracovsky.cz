  <h2 id="zal4">Možné vysvělení - chronologie</h2>
  <p>
  Jazzový saxofonista Fred Madison žije v chladně zařízeném domě se svou znuděnou ženou Renée. Když manželce ze svých 
  nočních cest za hraním volá, nebere mu telefon. Začíná ji podezírat, že vede dvojí život (viz dále jeho sen). 
  Jednou večer se manželé pomilují, ale už to není, co bývalo. Žena konejší Freda a on jí vypráví o svém snu: 
  hledal volající Renée v domě, našel ji v ložnici, ale nebyla to ona. Čím dál více se saxofonista užírá 
  nejistotou a žárlivostí. Navíc začíná propadat schizofrenii (kazety a minitelevizor představují ve Fredově 
  mozku paměť nového ega, tajemného muže). 
  </p>
  <p>
  Rozhodne se manželku sledovat a zjistí, že je mu opravdu nevěrná. Její milenec se jmenuje Dick Laurent, je mafián 
  a nechává si točit pornografické filmy. V jednom hrála i jeho žena Renée. Sleduje dvojici až k hotelu "Lost 
  Highway". Zde se ubytuje v pokoji č.25, zatímco se manželka a Laurent milují ve vedlejším pokoji č.26. Po 
  odchodu Renée Fred napadne jejího milence a zavře ho do kufru auta. Potom s ním odjede na opuštěné místo do 
  pouště. Pří následné rvačce saxofonista Laurenta zraní nožem. V tom okamžiku se mu zjevuje jeho druhé 
  schizofrenické já v podobě tajemného muže s bledě nalíčenou tváří. Dick se ptá, co po něm Madison chce. Jeho 
  druhé já samozřejmě nevidí. Fred prostřednictvím tajemného muže odpoví, co viděl a saxofonista (tajemný muž) 
  mafiána zastřelí. Fred utíká z místa činu a vrátí se domů, kde později psychickým vyčerpáním usne. Nad ránem 
  procitne, ale noční zážitek si už nepamatuje, protože ho vytěsnil do nevědomí (lépe řečeno ve Fredově těle 
  žijí dvě ega, což je Fred a tajemný muž, pokud získá kontrolu nad Fredovým tělem tajemný muž, tak si věci, 
  které se odehráli pod ním, pamatuje hlavně on, tajemný muž získává kontrolu, když se Fred rozzlobí; v tuto 
  chvíli má kontrolu nad svým tělem Fred, takže si ty události kolem sledování manželky a vraždy Laurenta 
  "pořádně" nepamatuje). Potom má zvukovou halucinaci, že slyší hlas v mluvítku, který mu oznamuje, že jakýsi 
  Dick Laurent je mrtvý. U dveří však nikdo není, což nechápe. 
  </p>
  <p>
  Večer se Madisonovi účastní párty u mladíka Andyho, jehož kdysi poznala Renée v klubu Moke´s. Fredovi se opět 
  zjeví jeho schizofrenické já v podobě tajemného muže. Ten tvrdí, že se s ním setkal v jeho domě a že právě 
  tam se teď nachází. Saxofonista na jeho radu zatelefonuje domů. Tajemný muž stojí před ním, a zároveň mu 
  skutečně odpovídá ze sluchátka. Fred sleduje své odcházející druhé já a ptá se Andyho, kdo že je ten záhadný 
  host. Andy se podívá naznačeným směrem. Fredovu představu nemůže vidět, ale na schodech se baví muž se ženou. 
  A právě o tomto muži prohlásí, že je přítelem Dicka Laurenta. Saxofonista odpoví podle toho, co slyšel z 
  mluvítka. Andy odporuje, že Laurent nemůže být mrtvý, protože v dané chvíli o jeho smrti neví. Když se 
  manželé vracejí domů, má Fred představu, že vidí v bytě podivné stíny. Nikoho však nenajde. V noci si vybaví 
  události včerejšího dne a jeho schizofrenické já zabije Renné.
  </p>
  <p>
  Při výslechu ve vyšetřovací místnosti si ale zase nic nepamatuje. Přestože tvrdí, že manželku nezabil, je 
  odsouzen k trestu smrti na elektrickém křesle. V cele trápí Freda vzpomínky na zohavené manželčino tělo. 
  Vězeňský lékař Smordin dá odsouzenci sedativa a saxofonista po představě - Z ohně a kouře vzniká dřevěná 
  chata. Jde o zpětný záběr vybuchujícího domku. <i>[Dřevěná chata představuje Fredův nemocný mozek. 
  Saxofonista měl ještě nedávno pocit, že se mu hlava rozletí, ale pod vlivem sedativ teď bolesti ustupují.]</i> - 
  usíná. 
  </p>
  <p>
  Pokud budeme vycházet z "Výkladu snů" od Zikmunda Freuda a jeho definice snu, která zní asi takto:
  <br> 
  <b>"Sen je iluzí vycházející z útržků reality s cílem plnit přání spícího."
  </b>,<br>
   tak by se Fredův následný sen dal vyložit 
  takto - Dozorce ráno najde ve Fredově cele jiného muže. Zjistí se, že je to čtyřiadvacetiletý Pete Dayton, 
  který byl odsouzen na podmínku a který má bydlet u svých rodičů. Ti si pro syna přijedou. <i>[Fred by chtěl být
   mladý, nevinný a hlavně propuštěný. Pete ze snu je Fred.]</i> Pete se shledá s kamarády a s milenkou Sheilou. 
   Nepamatuje si, co se s ním stalo, kde byl a odkud má rány na obličeji. Vrátí se do práce k majiteli 
   autoservisu, černošskému invalidovi Arniemu. <i>[Ve snu by chtěl mít obyčejné denní zaměstnání.]</i> Petea vyhledá 
   gangsterský boss pan Eddy. <i>[Dick Laurent je ve snu Eddy.]</i> Detektivové Hank a Lou, kteří mladíka neustále 
   sledují, přehlédnou, že pan Eddy vzal svého oblíbeného opraváře na projížďku mercedesem. Pete je svědkem, 
   jak Eddy brutálně zmlátí a poníží řidiče, který se mu "lepil na zadek". Mladík po návratu přijme peníze za 
   doladění motoru, ale odmítne si od bosse půjčit pornokazetu. <i>[Fred se bojí, aby na kazetě nebyla Renée.]</i> 
   Hank a Lou si až teď mafiána všimnou. Poznají v něm Dicka Laurenta. Dvojice detektivů sleduje Petea i v 
   noci, kdy se mladík miluje v autě se Sheilou. Pete v práci nemůže poslouchat Fredovo saxofonové sólo z 
   rádia. <i>[Nechce mít ve snu se svou profesí nic společného. Noční hraní umožnilo nevěru manželky.]</i> Eddy 
   přijede do servisu s cadillacem. Pete se na první pohled zamiluje do bossovy průvodkyně, krásné Alice 
   Wakefieldové. Ta je na rozdíl od tmavovlasé Renée blondýnka, ale jinak je Fredově ženě k nerozeznání 
   podobná. <i>[Alice ze snu je Renée.]</i> Žena večer přijede sama za Petem a svede ho.
  </p>
  <p>
  Milenci se začnou tajně scházet v hotelích (sledováni Hankem a Louem). Rodiče tvrdí Peteovi, že viděli, co 
  se s ním před časem stalo. Mladík přišel domů se Sheilou a s nějakým neznámým člověkem. O děsivém světelném 
