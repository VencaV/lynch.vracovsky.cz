  <h2 id="zal1">Obsah</h2>
  <p>
  Plavovláska Betty (Naomi Watts) právě přijela z rodného hnízda do Hollywoodu, aby se stala filmovou hvězdou. Setkání s 
  tmavovláskou bez paměti (Laura Elena Harring) ji vypravuje na cestu, z níž není návratu. Pátrá po ztracené identitě své nové 
  přítelkyně, nebo v drsném světě hollywoodských nočních můr ztrácí svou vlastní? Pilotní díl seriálu odmítnutého společností ABC 
  Lynch s podporou francouzské společnosti CanalPlus upravil do podoby celovečerního filmu. Prostřednictvím neřešitelné šarády
  vystavěné na půdorysu snu si autor pohrává s principy chronologie, vypravěčské logiky a totožností filmových postav. Navíc 
  poprvé otevřeně vypráví o absurdním světě jménem Hollywood.
  </p>
  <p class="autor">
  Autor: <a href="http://www.cinemamagazine.cz/o-redakci/425/alena-prokopova" target="_blank">Alena Prokopová</a>
  </p>
  <h2 id="zal2">Recenze</h2>
  <p>
  Lynchův film vznikl původně jako pilotní díl zamýšleného seriálu pro televizi ABC a byl o 26 minut kratší. Z projektu sešlo a 
  francouzská CanalPlus Lynchovi umožnila natočit dodatečné scény a vytvořit tak ucelený příběh. Slavný režisér tentokrát nahlédl 
  pod povrch lesku hollywoodské honorace. S jemnou ironií propojuje osudy panoptikálního světa „lidí od filmu“ s příběhem mladé 
  ambiciózní dívky, která se zde ocitla poprvé. Nezbytná kriminální zápletka, vláčné tempo vyprávění podporované vynikající hudbou 
  Angela Badalamentiho a perfektní herecké výkony vytvářejí na plátně postmoderně mystickou atmosféru v Lynchově typickém stylu. 
  Režisér dokonale využil prvků, které se objevily v jeho předchozích snímcích, a vytvořil svůj zatím pravděpodobně nejlepší film.
  </p>
  <p>
  Limuzína veze černovlasou elegantní ženu nočním Hollywoodem. V Mulholland Drive dojde k nehodě, vyděšená žena se skryje v křoví 
  u Sunset Boulevardu. Ráno hledá útočiště v luxusním bytě, ze kterého odjíždí herečka Ruth na natáčení. Z Kanady mezitím přilétá 
  Ruthina neteř Betty, v prázdném bytě najde jen neznámou, která po nehodě ztratila paměť a nechá si říkat Rita (podle plakátu s 
  Ritou Hayworthovou). Betty chce jako mladá herečka dobýt Hollywood, zatímco se chystá na casting, pomáhá Ritě s hledáním 
  ztracené identity. Mladý režisér Adam Kesher dostal od filmového magnáta Roquea prostřednictvím Vincenza a Luigiho 
  Castiglianeových nekompromisní pokyn. Má v natáčeném filmu přeobsadit hlavní ženskou roli mladou dívkou Camillou Rhodesovou. 
  Adam se nepodvolí a je z filmu vyhozen. Na dvorku za lacinou restaurací Winkies se dějí podivné věci, jak se mladík Dan snaží 
  vysvětlit příteli. Betty a Rita při svém pátrání objeví v domku na předměstí mrtvolu mladé dívky Diany Selwynové...
  </p>
  <p>
  Lynch se opět předvedl jako dokonalý mistr inscenace a pečlivé práce s divákovou pozorností. Jeho dvouapůlhodinový film s ne 
  vždy jasným, zato však v podstatě banálním dějem dokáže hluboce zaujmout od první až do poslední scény. Důmyslnou hrou s 
  obrazem a zvukem vznikl prokomponovaný celek bez slabých míst. Sledování příběhu je ale značně náročné, neboť maskována 
  zdánlivou reálností děje, odchyluje se jeho logika postupně více a více k surrealistickým vizím. Závěr pak ukazuje, že vše se 
  odehrálo v složité časoprostorové (pře)smyčce, zobrazený úsek je vlastně jen částí komplikované nadreality. Je proto vhodné 
  vidět film opakovaně, více tak vyniknou jeho kvality, ke kterým patří zejména výkony představitelů hlavních i vedlejších 
  rolích, dokonale zakomponované do celku a perfektní v detailech. 
  </p>
  <p>
  Z více či méně známých tváří, které se na plátně objeví, upoutají především obě herečky v hlavních rolích. Temperamentní 
  blondýna Betty v podání Naomi Wattsové a tajemná černovlasá Rita Laury Harringové se dokonale doplňují. Z epizodních rolí se 
  vryje do paměti Dan Hedaya jako lakonicky neústupný Vincenzo Castigliane a Lafayette Montgomery (jeho Kovboj je výstižnou 
  ilustrací toho, že kdo má dost peněz, nemůže být blázen...). Spolehlivé zázemí hlavním postavám poskytuje domácí Coco 
  (muzikálová veteránka Ann Millerová) a hollywoodská menažérie „velkých zvířat“, z nichž v lehce parodované castingové scéně 
  stojí za pozornost režisér Wally Brown (James Karen). Hlavní postavou druhé dějové linie je režisér Adam Kesher, kterému Justin 
  Theroux propůjčil přesně odměřenou dávku snobské nerovnosti a mladické nezkušenosti.
  </p>
  <p>
  Bohatstvím situací naplněný snímek obsahuje několik vrcholů; patří k nim úvodní jednání za zavřenými dveřmi o přeobsazení role 
  v Kesherově filmu, castingová scéna, která dopadne zcela jinak, než jak Betty očekávala, a konečně návštěva dívek v přízračném 
  nočním klubu Silencio. Tam již Lynch naplno povoluje své zálibě v zobrazování mnohovýznamových vizí a jako obvykle nedává 
  jednoznačnou odpověď na navozené otázky. Oproti svým dřívějším mysteriózním thrillerům tentokrát ubral drastické scény a přidal 
  ještě více na eleganci a uhlazenosti. Film tak získal mimořádnou vyváženost a působivost, což americká kritika i porota 
  festivalu v Cannes po zásluze ocenily. David Lynch patří k onomu vzácnému druhu režisérů, kteří dovedou tvořit komerční zábavu 
  na vysoké úrovni a sloučit ji s uměním.
  </p>
  <p class="autor">
  Autor: Stanislav Buzek, tiscali.cz
  </p>
  <h2 id="zal3">Z diskuzí...</h2>
  <ul>
  <li>Pro mě nejlepší Lynchův kousek, jaký jsem doposud viděl. Doporučuji si předem přečíst krátký popis k filmu, protože se jedná 
  o velice složité dílo. Obě dvě hlavní hrdinky předvedly velice solidní herecký výkon, ze kterého mi běhal mráz po zádech.
  <span class="uz">(Pardal, www.fdb.cz)</span></li>
  <li>Film o čistote ľudskej duše a o jej zraniteľnosti, o životných snoch, o túžbe naplniť očakávania, ktoré sú do nás 
  vkladané, o strachu dotknúť sa ideálu, o sklamaniach, zúfalstve a nenávisti, o škaredosti sveta tam vonku (tu konkrétne 
  Hollywoodu). Tento film nemá žiadnu jednu jasnú pointu, neexistuje k nemu žiaden "správny kľúč". MULHOLLAND DR. je mozaikou 
  viacerých myšlienok, ktoré by sa dali povedať aj oveľa jednoduchším, ale nie tak zaujímavým a pôsobivým spôsobom. To, koľko 
  súvislostí v ňom vám niečo povie, záleží od vašich osobných prežitkov a uvedomovania si ich miesta vo vašom živote. Krutý a 
  krásny film zároveň.
  <span class="uz">(POMO, www.csfd.cz)</span></li>
  <li>Většinu filmu to vypadalo, že sleduju "normální príběh", poslední půlhodinu však Lynch udělal rázný obrat a já jen 
  nechápavě zíral. Úplně mi zamotal hlavu. Docela mi pak pomohlo vysvětlení v komentářích k filmu na filmpubu. Ale i kdybych to 
  nepochopil, tak ten film obstojí, právě pro skvěle vybudovanou tajemnou atmosféru, zajímavé herecké výkony a dobrou hudbu 
  (Angelo Badalamenti si zde zahrál mafiána, který je nespokojený s kávou).
  <span class="uz">(Anthony, www.csfd.cz)</span></li>
  <li>Vynikajici atmosfera a propracovany scenar delaji z tohoto filmu mimoradny emocionalni a intelektualni zazitek. A reseni 
  vubec neni tak obtizne nebo dokonce neexistujici, jak se nekteri mylne domnivaji, i kdyz to tak nemusi vypadat.
  <span class="uz">(Gimli, www.csfd.cz)</span></li>
  <li>Tak z tohoto filmu jsem měl hodně zamotanou hlavu, na druhý den jsem si ho musel pustit znova a to už jsem si to začal 
  spojovat a určitě se na něj podívám ještě jednou. Dalši z filmů, který lze jen stěži popsat slovy, ale který vyvolává silné 
  dojmy, jako například scéna po rozhovoru dvou mužů v kavárně.
  <span class="uz">(Bouvard, www.fdb.cz)</span></li>
  </ul>
  
  <h2 id="zal4">Možné vysvělení</h2>
  <p>
  Diane Selwyn žijící v kanadském městečku Deep River vyhrála tamní taneční soutěž. Její teta bydlící v L.A. zemřela a odkázala 
  ji peníze. Diane odlétá do L.A., kde se chce stát úspěšnou hollywoodskou herečkou. Ubytuje se v nájemním bytě a zúčastní se 
  hereckého konkurzu na hlavní roli ve filmu "Příběh Sylvie Northové". Starší režisér Bob Broker ale hlavní roli obsazuje lepší 
  herečkou - Camillou Rhodes. Diane se seznámí s Camillou a jejich přátelství přeroste v lesbickou lásku.
  </p>
  <p>
  Camilla se stává úspěšnou herečkou a ve filmech, kde hraje hlavní role, prosazuje do vedlejších rolí Diane. Mladý režisér Adam 
  Kesher si vybírá pro hlavní roli svého nového filmu Camillu. Adam přistihne svoji ženu při nevěře, rozvádí se a od soudu 
  odchází jako vítěz - získal většinu majetku a dům. Při natáčení se zamiluje do Camilly. Camilla postupně ukončuje vztah s 
  Dianne. Pozve ji do Adamova domu na párty. Muži, kteří limuzínou vezou Diane, nečekaně zastaví na Mulholland Drive. Diane se 
  lekne, ale vzápětí se vše vysvětlí - překvapení si vymyslela Camilla, která odvádí Diane zkratkou do domu. V domě se Diane baví 
  s Adamovou matkou a Adam oznamuje přítomným, že se žení s Camillou. Při tom si Diane všimne odcházejícího muže v covbojském 
  klobouku.
  </p>
  <p>
  Diane se Camille, za zhrzenou lásku a za svůj herecký neúspěch ze kterého ji viní, rozhodne pomstít. V restauraci Winkies se 
  setká s nájemným vrahem, kterému předá fotku Camilly a tetiny peníze. Vrah řekne Diane, že potom co vraždu provede, pošle klíč 
  a ukáže ho. Diane se ptá od čeho je, této absurditě se on směje. Diane si všimne, že servírka, která je obsluhuje se jmenuje 
  Betty a setká se pohledem s neznámým mužem, který sice neví, že se domlouvá nájemná vražda, nicméně to nevědomě vycítí. Od té 
  doby tohoto muže, jmenuje se Dan, pronásleduje hrůzostrašný sen. Sedí v této restauraci u stolu, kde seděla Diane s vrahem a za 
  přítelem, který platí u pultu, stojí za zdí chlap s děsivou tváří. Aby se noční můry zbavil, pozve svého přítele do restaurace. 
  Přítel se jmenuje Herb a po vyslechnutí tohoto snu jdou zkontrolovat, co je za zdí. Dan dostane infarkt způsobený jeho 
  představivostí a umírá. Vrah vykonal zakázku a posílá klíč Diane. Diane se přestěhuje do vedlejšího bytu. Lituje svého činu, 
  protože přes všechno měla Camillu ráda. Vezme si prášky a usne.
  </p>
  <p>
  Pokud budeme vycházet z "Výkladu snů" od Sigmunda Freuda a jeho definice snu, která zní asi takto: "Sen je iluzí vycházející z 
  útržků reality s cílem plnit přání spícího.", tak by se Dianenin následný sen dal vyložit takto:
  </p>
  <p>
  Žena, která jede v limuzíně po Mulholland drive je Camilla. Má její tvář a ve snu si bude říkat Rita. Realitou je, že ve 
  stejné situaci Diane byla. Muži chtějí Ritu (Camillu) zabít, ale nepovede se jim to. Diane by chtěla ve svém snu Camillu 
  zachránit, vrátit své rozhodnutí zpátky. Ve snu se Diane jmenuje Betty, protože si toto jméno přečetla v restauraci Winkies. 
  Ve snu teta odjíždí z domu do kterého se Betty (Diane) stěhuje. Diane konstatuje její smrt (odjela - zemřela) a chtěla by se 
  nastěhovat do pěkného baráku na Mulholland Drive jako Camilla. Betty (Diane) oslní svým hereckým uměním na konkurzu. Ve 
  skutečnosti to Diane asi zvorala nebo nemá talent, protože jinak by ji Camilla coby úspěšná herečka a její milenka nemusela 
  shánět vedlejší role (viz. rozhovor s Adamovou matkou). Vinu za to, že se nemůže prosadit, ale dává filmovým bossům a jejich 
  intrikám.
  </p>
  <p>
  Rita (Camilla) má v kabelce modrý klíč. Klíč představuje smrt pro Camillu. Ve snu se pouze oproti skutečnosti jeví jako větší 
  a modrý. Diane byla v reálu zvědavá od čeho je, tudíž ve snu vzniká spojení modrý klíč - modrá skřínka. Ve snu je Adam vyhozen 
  ze svého domu, je špinavý a vypadá to, že je na mizině. Diane by si to takhle přála za to, že ji připravil o lásku Camilly. Ve 
  skutečnosti je Adam bohatý a z rozvodu finančně vytěžil. Covboj - muž, kterého Diane zahlédla na párty. Ve snu vystupuje jako 
  posel varování od filmových bossů. Coco - matka Adama. Na párty byla k Diane schovívavá. Ve snu se proto objevuje jako hodná 
  paní domácí.
  </p>
  <p>
  Silencio club - má naznačit, že Diane spí. Když se Diane ze snu probudí, uvědomí si opět realitu. Předá sousedce, se kterou si 
  vyměnila byt, její věci a pak vzpomíná na Camillu a vidí ji v představě. Výčitky svědomí, nevratnost rozhodnutí a strach z 
  potrestání (sousedka se zmínila, že Diane hledají detektivové) nakonec vedou k sebevraždě.
  </p>
