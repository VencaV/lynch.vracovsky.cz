  <h2 id="zal1">Obsah</h2>
  <div class="citace">
  V bezejmenném městě sužovaném neustálým deštěm žijí tři králíci se strašlivým tajemstvím...
  </div>
  <p>
  Rabbits se odehrávají v jedné místnosti, celá epizoda je vždy jediným záběrem statické kamery bez jediného střihu. Vypráví o 
  rodině králíků, žijících v této místnosti. Dialogy (či monology) králíků, které se zdají nesmyslné jsou doprovázeny dlouhým 
  potleskem a smíchem jako v sitcomech. Stejně jak u většiny Lynchových filmů je také u Rabbits hlavně na divákovi, jak si jeho vize 
  a obrazy podané tentokrát prostřednictvím zvláštní králičí rodinky vyloží.
  </p>
  <h2 id="zal2">Z diskuzí...</h2>
  <ul>
  <li>Fascinující! Ta hypnotizující atmosféra! Lynch vytvořil omalovánky, které si každý může vybarvit podle vlastního vkusu a 
  představ... Tohle si nesmí žádný fanoušek nechat ujít!!!
  <span class="uz">(EdaS, www.csfd.cz)</span></li>
  <li>Vskutku bizarní alternativa seriálu. Lynch dokáže i přes zcela minimalistické pojetí zkomponovat napětí a absurdní humor. 
  Pusťe do televize namísto Rodinných pout, zajímalo by mne, jak by diváci primácké telenovely pobrali tuhle rodinku.
  <span class="uz">(kinej, www.csfd.cz)</span></li>
  <li>Scéna - 1 pokoj, rodina králíků, naprosto náhodné dialogy, zvuky, které by za hudbu mohl označit jen šílenec... 
  1 statický záběr. Naprosto strašidelné. Důkaz toho, že i z ničeho lze udělat velké něco.
  <span class="uz">(anais, www.csfd.cz)</span></li>
  <li>Super, ale jakýsi "děj" už si musíte dát dohromady sami, možná právě za pomoci bizarních králičích popěvků. Ale jestli mají 
  napovídat, symbolizovat nebo zavádět, na to nikdo neodpoví - viz úryvek: "Temná místnost. Něco je špatně. Zima. Ticho. Tmavá, 
  usměvavá hlubina. Pohyb prsty. Kouř, olej, teplo. Rozmazaná krev. Otevřela jsem temnotu."
  <span class="uz">(Ingrid, www.csfd.cz)</span></li>
  <li>Musim se přiznat, tenhle film sem zřejmě docela nepochopil... koukal jsem na něj snad tisíckrát. Prostě jen jedna místnost, 
  jeden třičtvrtě hodiny trvající statickej záběr a rodinka králíků, kteří žijí obyčejný život ve městě, kde pořád prší. Ale vlk 
  se blíží, a to pro králíky není dobré...
  <span class="uz">(Rosomak, www.fdb.cz)</span></li>
  <li>Lynch zase zabrouzdal do svého tajemného nitra a vytvořil seriál, který opět nemá obdoby. Nechápu čím to je, ale Lynch umí 
  do filmu dát něco tak strašně mysteriózního, že i když je film možná dějově o ničem, vám se dostane něco nepopsatelného, co umí 
  právě jen Lynch. Fantastická, tajemná, místy nesmírně syrová a chladná atmosféra. Perfektní.
  <span class="uz">(solskjaer, www.csfd.cz)</span></li>
  <li>Film s prvky absurdního divadla, to se opravdu často nevidí, i když u Lynche to není zas tak překvapivé. Musím uznat že i 
  na mě atmosféra docela zapůsobila. Opravdu nemá cenu hledat nějaký větší smysl, tohle je čistě pocitová záležitost, a jako 
  taková si zaslouží kladné ohodnocení.
  <span class="uz">(raffspln, www.csfd.cz)</span></li>
  </ul>
