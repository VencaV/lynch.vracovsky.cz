  <ul>
  <li>Povinnost pro milovníky hudby Angela Badalamentiho. Muzikál je snem pro milovníky Badalamentiho 
  a noční můrou pro příznivce muzikálů, jako Dracula, či Kleopatra. Hudba je hlavní devizou filmu. Místy libé 
  tóny, místy neurotický klarinet doprovází brutální scénografie Lynche, chvílemi připomínající bad trip.
  <span class="uz">(kinej, www.csfd.cz)</span></li>
  <li>Když posloucháte muziku, dostanete se v ideálním případě do stavu absolutní volnosti, oproštěnosti od těla 
  a vybavují se vám různé obrazy a (vý)jevy. Badalamentiho hudba k tomu přímo vybízí. Tentokrát představy 
  "vyrobil" David Lynch za vás; nastolil atmosféru starých amerických klubů 50. let, kde se po podlaze klouže 
  kouř a opodál stojí rozbité auto. Hlavní zpěvačka s božským hlasem Julee Cruise se převážně vznáší vzduchem a 
  zpívá. Není nutné ji sledovat, stačí se naladit na správnou vlnu a nechat se unášet. Vizualizace skladeb se 
  ovšem moc nemění, a tak oceníte drobné nápady jako krátkou detektivku a řezání dřeva (narážka na Twin Peaks), 
  tancující dívky a nakonec snad, že to má jen těch 50 minut.
  <span class="uz">(Silas, www.csfd.cz)</span></li>
  <li>Úvodem bych chtěl zmínit, že Julee Cruise má hlas jako anděl - přestože jsem anděla ještě nikdy neslyšel, 
  a někdo může dokonce pochybovat o jejich existenci; každopádně bych toto přirovnání dále nerozebíral a patřičné 
  kacíře odkázal na film City of Angels;). Hudba je dokonalá a děj, korigovaný taktovkou Davida Lynche ji 
  bezchybně doplňuje. Nebo je tomu snad naopak? Hlavní je, že psychicky narušený divák se dobře baví. Já se bavil 
  nadmíru dobře.
  <span class="uz">(Eddie003, www.csfd.cz)</span></li>
  <li>Připadal jsem si jak na tripu. Vážně hodně avantgardní muzikál. Přece jenom dám radši přednost klasice. 
  Ale originalitu tomu vzít nemůžu.
  <span class="uz">(pepino, www.csfd.cz)</span></li>
  <li>To přirovnání k tripu je opravdu na místě. Je to zase ta jedinečnost všeho, co Lynch vytváří, tentokrát 
  mnohem více dokreslovaná hudbou, která snad nahrazuje i tu nepatrnou dějovou linku, co se u jiných jeho filmů 
  dá s trochou štěstí občas najít.
  <span class="uz">(Willow666, www.csfd.cz)</span></li>
