  <h2 id="zal3">Z diskuzí...</h2>
  <ul>
  <li>Typické Lynchovo psycho, na které jsme u něj zvyklí. Podle mě je to jeho nejlepší film. Ne že by ty 
  ostatní nebyly dobrý, ale tenhle se dá aspoň pochopit a zorientovat se v něm. Navíc je to krásný snímek o tom, 
  v jak divném světě to vlastně žijeme, o tom jak jedině láska může ukončit naše trápení. Jako třešnička na dortu 
  pak působí herecká kreace Dennise Hoppera.
  <span class="uz">(nash, www.fdb.cz)</span></li>
  <li>Má oblíbená Lynchovina. Opravdu těžce přehuštěný film, kdo nemá rád Lynche, tak to asi 
  nerozchodí. Neskutečným způsobem ulítlý Dennis Hopper, Kyle MacLachlan ve své asi nejlepší filmové roli, 
  Isabella Rosselini je taky úžasná a tak bych mohl pokračovat i o dalších hercích, z nichž většna jsou 
  přesné typy pro tento druh filmu. Nedokážu vysvětlit, proč je ten film tak geniální, ale prostě je.
  <span class="uz">(Radyo, www.csfd.cz)</span></li>
  <li>Film je rozhodně inteligentní, neservíruje všechno divákům přímočaře, jednoznačně; nutí je myslet a 
  nedělá z nich blbce. Našlo by se sice pár drobností, které mi zrovna neseděly, ale to jsou opravdu takové 
  detaily, že celkový dojem nezkazily - jinak herci výborní, Badalamentiho hudba mi také seděla, moc se mi 
  líbila sugestivní kamera a vůbec celá atmosféra snímku. Navíc bych řekla, že čím déle o filmu přemýšlím, 
  tím víc se mi zdá zajímavý a začínám mít nutkání jít si ho pustit znovu. A to je ten nejlepší znak 
  dobrého filmu.
  <span class="uz">(Galadriel, www.csfd.cz)</span></li>
  <li>Nádhera. Atmosféra by se zase dala krájet a k tomu skvělý Kyle MacLachlan, neuvěřitelný sympaťák, který 
  si vás získá když předvádí slepičí chůzi. A pak ďábelský Dennis Hopper. Píseň Blue Velvet vám opravdu udělá 
  díru do hlavy. Zkrátka nejlepší Lynch.
  <span class="uz">(Rob Roy, www.csfd.cz)</span></li>
  <li>Jeden z nejlepších Lynchových filmů... vlastně jeden z nejlepších filmů vůbec... Dokonalá atmosféra, 
  fantastická zápletka a bezchybné obsazení, ať už se jedná o oceněného Dennise Hoppera nebo o Isabelu Rosselliny 
  nebo o Kylea a další! Modrý samet je jeden z mála Davidových filmů, kde lze vše téměř bez problémů chápat, je méně 
  složitější, ale to mu neubírá na přitažlivosti. Neopdpustil si drásavou hudbu a jeho typické scény (při jízdě autem 
  na silnici, či pohled na ukazovatele za přítomnosti depresivních tónin...) Nezbývá mi než zatleskat velkému umělci 
  a skvěle odvedeným výkonům všem spolupracujícím na filmu. Dávám 100%, protože nenacházím snad jedinou chybu...
  <span class="uz">(Gabra, www.kfilmu.net)</span></li>
  <li>Tohleto je naprosto báječná Lynchovy vlastní úchylárna, Roselliny s Hopperem jsou naprosto skvělý, bez nich by 
  film dostal o dva body méně, já tu Lynchovu depresivitu ponurost a hlavně tajemnost prostě miluju, je to pan 
  tvůrce který se drží svého...
  <span class="uz">(Pardal, www.fdb.cz)</span></li>
  <li>Tajemný pohled Davida Lynche na dokonalost, čistotu a krásu amerického města a jeho obyvatel. Pomalu se 
  odhalující "podsvětí" do kterého proniká Kyle MacLachlan. Již od úvodní scény je patrné, že nám David 
  Lynch ukáže, co se vše může skrývat pod krásou (krásný zelený opravovaný trávník) a zlo v něm (hnusní odporní 
  brouci). Dále je tu láaska, která v tomto filmu hraje také svou roli. Láska (drozdi) všechno zlo a trápení 
  (brouci) zaženou. Z toho je jasný závěrečný význam příletu drozda. Postava Franka je zastrašující, děsivá, 
  arogantní, ale také vyvolává jisté touhy. A to vše, celé toto podsvětí viděno díky zločinu (vloupání, 
  voayerismus). V tomto filmu se rychle ztrácí ideje. Bezchybná hladká a děsivá kamera. Dokonalá atsmoféra, 
  neuvěřtitelně hustá; očekávání každé události se spojuje s bušením srdce. Hudba též nemá chybu. Mistr Lynch si 
  opět vybral svůj den a natočil geniální atmosférický film.
  <span class="uz">(Djkoma, www.csfd.cz)</span></li>
  <li>Lynche nemám zrovna v lásce a ani jej nepovažuji za dobrého režiséra. Stejně jako ve Zběsilosti v srdci jsem 
  měl i tady pocit, že postavy jsou jen umělé figury, které jsou ve snímku jen proto, aby nebyl nudný. Samotný 
  příběh jakoby se odehrával úplně někde jinde, ten hrdinů totiž není vůbec podstatný. Herecké výkony tomu 
  napovídají, stejně tak jako nasnímání protagonistů a vůbec všechno okolo. To ale nic nemění na faktu, že Modrý 
  samet se Lynchovi opravdu povedl. Dočkáme se nádherných scenérií a dokonce i úžasného hudebního doprovodu...
  <span class="uz">(Dudek, www.csfd.cz)</span></li>
  <li>Celé je to filmařský skvost se vším všudy, ale scéna v níž se hlavní hrdina ukryje do skříně, aby byl svědkem 
  něčeho hrůzného, je geniální - všeobecně jedna z mých oblíbených horrorových situací.
  <span class="uz">(Mr.Apache, www.csfd.cz)</span></li>
  </ul>
  <h2 id="zal4">Zajímavosti</h2>
  <ul>
  <li>Role Jeffreyho byla nabídnuta Val Kilmerovi, ale ten ji odmítnul s tím, že jde o pornografii. 
  I když později prohlásil, že verzi, kterou viděl pak v kině by natáčel moc rád.</li>
  <li>Několik herců odmítnulo roli Franka s tím, že jim přijde vyloženě odpuzující. Dennis Hopper 
  ji naopak přijal s tím, že on musí hrát Franka, protože on <b>je</b> Frank!</li>
  <li>Původně chtěl Lynch, aby Frank inhaloval během sexuálních scén s Dorothou.</li>
  <li>Roli měla původně hrát Hanna Schygulla, když odmítnula přemýšlel Lynch o oslovení Helen Mirren, 
  ale pak potkal modelku Isabellu Rossellini v New Yorské restauraci.</li>
  <li>Rossellini byla pod sametem nahá během scény znásilnění. Dennis Hopper to poznal až v momentě, kdy ji dal 
  další herec kolena od sebe, aby mezi ně mohl Hopper pokleknout. Šlo o první společnou scénu dvou herců, která 
  se natáčela.</li>
  <li>Producenti nechtěli zaplatit za práva ke skladbě "Blue Velvet" Bobbyho Vintona. Proto dostal Angelo 
  Badalamenti za úkol nahrát novou verzi písně, která by ale zněla jako ta stará. Přizvali pak Vintona, 
  aby znovu píseň nazpíval, i když o půl tóniny níž. I když se Lynchovi výsledek zamlouval, přemluvil 
  nakonec studio, aby koupilo práva na původní skladbu.</li>
  <li>Producent Dino De Laurentiis byl nucený založit vlastní distribuční společnost D.E.G., aby dostal 
  film do kin. Žádná jiná společnost s ním nechtěla mít co dočinění.</li>
  <li>Scéna, ve které Dennis Hopper uhodí Isabellu Rossellini byla editována tak, aby dopad dlaně na tvář nebyl 
  vidět. Úprava byla nutná, aby nedošlo k protestům ze strany MPAA skrz násilí na ženách. Lynch však trval na 
  tom, že editací se scéna ještě umocnila.</li>
  <li>David Lynch zkrátil film z původních čtyř hodin na polovinu. I přes několikeré pokusy se vystřižené dvě 
  hodiny filmu nikdy nepodařilo najít. Dochovalo se pouze několik fotografií.</li>
  <li>Roy Orbison nechtěl dát souhlas k použití své písně "In Dreams" ke scéně v nevěstinci. Lynch našel legální 
  způsob, jak ho obejít, práva na použití dostal, a že je skutečně skladba ve filmu objevil Orbison až při 
  shlédnutí snímku. Později však ke skladbě nechal natočit video, které produkoval Lynch a ve kterém se objevilo 
  několik záběrů z filmu.</li>
  <li>Dennis Hopper řekne slovo "fuck" téměř v každé větě. Nikoho jiného toto slovo říct ve filmu neuslyšíte, 
  s výjimkou Deana Stockwella, který ho však vysloví na příkaz Hoppera.</li>
  <li>Postava Franka měla původně vdechovat helium, ale jeho představitel Dennis Hopper přišel s myšlenkou, 
  že bude lepší dusičnan škrobovitý (?), který údajně umocňuje sexuální prožitek. Až po letech napadlo Hoppera, 
  jak by na plátně vypadalo bizarně, kdyby po scéně chodil maniak s vysokým hlasem a že to byl asi 
  původní záměr.</li>
  </ul>
