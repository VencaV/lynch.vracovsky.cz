  <h2 id="zal1">Obsah</h2>
  <p>
  Maloměstský středoškolák Jeffrey Beaumont (Kyle MacLachlan) najde na louce lidské ucho a při pátrání po 
  tajemství zpěvačky Dorothy Vallensové (Isabella Rosselliniová) se zaplétá do světa noci a zločinu. 
  Svět amerického rodinného filmu se mění v peklo na zemi, kam jako anděl sestupuje Jeffreyho spolužačka Sandy 
  (Laura Dernová) a kde jako ďábel funguje sadistický psychopat Frank (Dennis Hopper). Vyprávění nabité 
  psychoanalytickými symboly se může (ale také nemusí) vykládat jako noční můra o hrdinově dospívání v muže. 
  Proslulé finále s vycpaným drozdem nesoucím v zobáku brouka ovšem naznačuje, že tento svět může být i snem 
  ve snu.
  </p>
