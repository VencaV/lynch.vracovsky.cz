  <p>
  Tento film byl natočen pro 46.MFF v Berlíně u příležitosti stého výročí vynálezu kamery bratří Lumièrů. 40 režisérů z rozličných 
  zemí světa dostalo možnost natočit svůj příspěvek do složeného filmu Lumière &amp; Company původní, sto let starou kamerou. 
  Zadání bylo pro každého režiséra stejné&nbsp;- maximálně 52 sekund, tři záběry a žádný synchronní zvuk. Snímky jsou opravdu 
  rozličné&nbsp;- jednoduché záběry na město nebo krajinu, stručné reminiscince uplynulých sta let kinematografie, pokusy o krátký 
  příběh, vtip...  
  </p>
