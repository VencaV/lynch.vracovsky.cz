  Příběh začíná objevením mrtvého těla zavražděné středoškolačky Laury Palmerové. Vyšetřováním její smrti je pověřen zvláštní agent 
  Dale Cooper (Kyle MacLachlan). S postupujícím vyšetřováním se odhalují příběhy, povahy a různá tajemství obyvatel (ne)obyčejného 
  Amerického městečka Twin Peaks. David Lynch je spolu se spisovatelem Markem Frostem autorem námětu, režíroval pilotní díl i několik 
  epizod seriálu a objevil se v něm jako šéf agenta Coopera Gordon Cole. Seriál se stal doslova kultovní záležitostí.
