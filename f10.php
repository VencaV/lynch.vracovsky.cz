  zdály být. Při vyslovení tak běžného jména jako je BOB mi běhá mráz po zádech. Snad ani nedokážu vymezit, svou nejoblíbenější 
  scénu (červený pokoj, školní třída po oznámení Lauřiny smrti, Leeland dozvídající se o zavraždění své dcery...) nebo postavu 
  (agent Cooper, Audrey, Andy, Donna...)... A vy? Čím je pro vás Twin Peaks? Pro mě jednoznačně závislost (i když v mém těle i 
