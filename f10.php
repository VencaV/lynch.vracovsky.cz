  <h2 id="zal2">Z diskuzí...</h2>
  <ul>
  <li>Skvost všech skvostů a kult všech kultů. Nepřekonaný a snad i nepřekonatelný seriál, jenž dal nový rozměr slovu "televizní". 
  David Lynch by měl přijít do nebe (nebo do bílého vigvamu, chcete-li) už jen za tohle své dílo, ani by nemusel natočit nic 
  jiného. Kyle McLachlan se už nikdy potom nepřekonal, stejně jako řada dalších známějších i neznámých herců z této fantaskní 
  série.
  <span class="uz">(Radyo, www.csfd.cz)</span></li>
  <li>Twin Peaks je podle mě nejlepším seriálem americké produkce který byl kdy natočen. Například nyní veleúspěšný seriál Lost 
  (Ztraceni) si z velké části bere inspiraci z Twin Peaks (taky je to úzká skupina lidí, mysteriózní jevy atd.). Hodně velkou 
  roli na kvalitě seriálu má režie Davida Lynche, který si v něm dokonce sám zahrál (roli šéfa agentů Gordona Colea). Dalším 
  kladem je vynikající český dabing seriálu.
  <span class="uz">(Kuba, www.radiotv.cz)</span></li>
  <li>První série dokonalá, zbytek je jen velice dobrý.
  <span class="uz">(Douglas, www.csfd.cz)</span></li>
  <li>To je asi nejlepší počin Davida Lynche vůbec. Twin Peaks zůstane navždy nepřekonáno!
  <span class="uz">(Eluin, www.csfd.cz)</span></li>
  <li>
