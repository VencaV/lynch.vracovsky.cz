  <h2 id="zal3">Z diskuzí...</h2>
  <ul>
  <li>Líbilo se mi, že jsem si chvilku myslel - tak a mám jasno! Ale Lynch tyhle myšlenky umí rychle vyléčit. 
  Hned přišel mrak a vše je v oparu - jako normálně :-)
  <span class="uz">(Golfista, www.csfd.cz)</span></li>
  <li>Filmy tohoto druhu jsou dost divácky náročné a nechci určitě tvrdit, že všemu v tomto filmu rozumím. A to 
  nemluvím o navazujícím seriálu. Přece jenom žijeme v jiném regionu a všední život je asi o něčem trochu jiném 
  nežli ve filmech. Ale rozhodně je to zajímavý zážitek a dá se na to dívat i několikrát, aniž by to ztratilo na 
  působivosti a zajímavosti.
  <span class="uz">(Udecek, www.kfilmu.net)</span></li>
  <li>Miluju seriál - pro skvělé postavy, především agenta Coopera a hlavně pro tu kombinaci humoru a tajemna a 
  vůbec všech možných žánru dohromady. Tady se bohužel humor vytratil, Cooper tu je jen symbolicky, někteří 
  moji oblíbenci chybí a film se nese v až příliš jednostranně temném duchu.
  <span class="uz">(Anthony, www.csfd.cz)</span></li>
  <li>Přiznávám, že ze seriálu jsem před shlédnutím filmu neviděl jediný díl. Když jsem navíc četl, že jde o 
  typickou Lynchovinu, trošku jsem váhal, jestli mám film vůbec shlédnout. Díky neznalosti seriálu mi tuším 
  ledacos uteklo, ale přesto to byl zajímavý filmový zážitek, který mě opět utvrdil v tom, že u Lynche méně 
  znamená více. Čím méně u jeho filmů přemýšlíte, tím více z nich máte. PS: Na film v žádném případě nekoukejte 
  dříve, než na seriál, pokud se chystáte k jeho shlédnutí.
  <span class="uz">(Berg.12, www.csfd.cz)</span></li>
  <li>Mistrně zpracovaný film. "Úchylné" pojetí Lynche se nezapřelo. Je pravda, že ať film nebo navazující seriál není jen 
  tak pro někoho, ale po nějakým čase mě tenhle film i seriál velice zaujal a sledoval jsem to se zaujetím. Velmi dobře 
  zpracovaný příběh a hodně náročných scén, kdy člověk musí skutečně přemýšlet o tom co vlastně viděl.
  <span class="uz">(Stefa Anovcin, www.kfilmu.net)</span></li>
  <li>Lynch je Bůh. Nebo spíš ďábel. Jeden z nejlepších "strašidelných" thrillerů, co jsem viděl. Ale jsem rád, 
  že jsem ho viděl až po seriálu. Hudba Angela Badalamentiho je opět skvělá. Trochu mě mrzela neúčast původní 
  Donny a úplná absence Audrey a pár dalších.
  <span class="uz">(Jhershaw, www.csfd.cz)</span></li>
  </ul>
