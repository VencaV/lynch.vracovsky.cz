  <h2 id="zal3">Z diskuzí...</h2>
  <ul>
  <li>Zajímalo by mě, kolik lidí na začátku filmu na něj koukalo jako na "nestvůru" a na konci filmu jim už ani nepřišlo, že by 
  viděli něco zvláštního či hrůzného... V tom je podle mě síla tohoto filmu. Člověk má prostě podvědomě strach z anomálií a 
  z něčeho nového, nezvyklého.
  <span class="uz">(potlepa, www.csfd.cz)</span></li>
  <li>Další z Lynchových zamyšlení nad tím, že největší zvířata/bestie se potulují někde hluboko v nás. Tentokrát vše 
  předloženo (na Lynche netradiční) nezběsilou formou; o to více však smutnou a burcující. Dojemný příběh o důstojnosti, 
  přátelství, radosti ze života, ale taky hyenismu, nelidskosti, utrpení a citové zvrácenosti. Lynchův opus netrpí žádnými 
  školáckými chybami a je v pravém slova smyslu brilantně natočen. Herecké výkony jsou zdrcující a totálně odzbrojující. 
  Dokonalé vyznění celého snímku je navíc posíleno tím, že je vše předloženo bez zbytečných moralit. Navíc či právě proto: 
  případné divákovy (chápejte: i mé) slzy nejsou Davidem Lynchem z očí násilně tlačeny a ždímány, ale jsou jaksi zcela 
  přirozenou reakcí organismu. Shrnuto a podtrženo: rozhodně jeden z nejdojemnějších, nejsmutnějších a nejkrásnějších filmů; 
  a to i přesto, že ona krása není patrná navenek, ale je ukryta někde hluboko, hluboko pod námi viděným vzezřením.
  <span class="uz">(zputnik, www.csfd.cz)</span></li>
  <li>Vynikající balada o životě člověka, který byl nádherný a zároveň ohavný. Příběh o muži, který byl celý život primárně využíván 
  jako atrakce, a to všemi lidmi, jak prostými dělníky tak smetánkou. I jeho přátelství s doktorem vzešlo z využití jeho 
  odlišnosti. Prostě příběh o tom jak tragédie jedince může být pro jiné komedií, obchodem a nebo společenskou záležitostí. 
  Na konec mohu jen dodat že 5 hvězdiček je asi málo. Zde by se hodilo asi jen MŇAM!
  <span class="uz">(Edito, www.csfd.cz)</span></li>
  <li>Jsem nadšen ! Děkuji, pane Lynchi. Naprosto excelentním způsobem navozená atmosféra, takže se člověk během filmu cítí, jako by 
  v tom 19. století skutečně zrovna pobýval. John Hurt v roli znetvořeného muže opravdu září. Ale i jej zastiňuje Anthony 
  Hopkins coby lékař, jenž se rozhodl onu lidskou zrůdu probádat. Psychologie postav je propracovaná a nikdo ve filmu není jen 
  "do počtu". Zkrátka - nutno vidět.
  <span class="uz">(Radyo, www.csfd.cz)</span></li>
  <li>S prehľadom najlepší Lynch (aj keď dosť nelynchovský). Herecký koncert Hopkinsa a Hurta v príbehu o skutočných ľudských 
  hodnotách a emóciách. Emocionálna bomba!
  <span class="uz">(Marius, www.csfd.cz)</span></li>
  <li>David Lynch mě bude překvapovat asi svým každým filmem... Nikdy bych neřekl, že právě on mě dojme až k slzám... Neuvěřitelně 
  hluboký a lidský film, který mohl lehce sklouznout k frašce, mě jednoduše dostal. Výkony Johna Hurta a Anthonyho Hopkinse jsou 
  dechberoucí... Při mnoha scénách se mi chtělo křičet nad nespravedlností světa a slzy se mi draly do očí... A konečně při 
  závěrečné scéně podbarvené úžasným Adagio For Strings emoce vypluly nezadržitelně na povrch.
  <span class="uz">(Tyler, www.csfd.cz)</span></li>
  </ul>
