  <h2 id="zal1">Obsah</h2>
  <p>
  Životní příběh Johna Merricka (John Hurt), který skutečně žil na sklonku minulého století v Londýně a který 
  se narodil děsivě znetvořený multiplexní neurofibromatózou, vypráví Lynch s citem pro existenciální situaci 
  titulního hrdiny. Vzdělaný i inteligentní Merrick se díky pochopení londýnského chirurga Trevese (Anthony 
  Hopkins) z ponižovaného pouťového monstra stal obdivovanou atrakcí vyšší londýnské společnosti. Film byl 
  natočen „na objednávku“ a stal se komerčním hitem, ale tématem soucitu ke zrůdným outsiderům tohoto světa a 
  viktoriánsky temnou vizualitou prohlubuje zákruty lynchovského labyrintu zla.
  </p>
  <p class="autor">
  Autor: <a href="http://www.cinemamagazine.cz/o-redakci/425/alena-prokopova" target="_blank">Alena Prokopová</a>
  </p>
  <h2 id="zal2">Recenze</h2>
  <p>
  David Lynch není autor, jehož díla pouštíte či doporučíte svým rodičům anebo prarodičům. Atakovat smysly střední a starší 
  generace Lynchovou černobílou surrealistickou prvotinou Mazací hlava znamená vystavit toto publikum s minimálně vycvičenou 
  estetickou distancí nebezpečí srdečního ataku. Městečko Twin Peaks je tak podivné, až se v něm konvenčně usazená mysl ztrácí 
  a nenachází dost opěrných žánrových bodů typického seriálu. Modrý samet a Zběsilost v srdci obsahují tolik perverzí, vulgarit 
  a otevřeného násilí, že v nich (nebo přes ně) jen dostatečně liberální člověk zahlédne určitou uměleckou koncepci, podvratně 
  užívající freudovskou symboliku. Lost Highway a Mulholland Drive jsou veskrze milými formálními podvody, které otevřeně 
  prozrazují svou klamavou strukturu, ale „běžný divák“ (zlaté tele všech rychlokvašených manažerů – rádoby sociologů) chce 
  příběh pochopit, chce v něm najít nějakou prostou logiku a spojitosti a ne se spokojit s tím, že dílo může svá témata jako 
  jsou schizofrenie, rozštěp osobnosti nebo ztráta paměti a potlačená touha manifestovat tím, že se prezentované události rozštěpí 
  na dva nesouvislé bloky, že se ztratí hranice mezi realitou a ireálným a že jsme v náznacích děje dezorientováni jako v 
  labyrintu zrcadel.
  </p>
  <p>
  A přesto i David Lynch má na svém kontě snímek, který získal osm nominací na Oscara a jenž bez obav můžete zapnout v televizi, 
  nebo na něj poslat do kina takřka kohokoli; je přístupný jako stavebnice Merkur od devíti do devětadevadesáti let. Ani přímočarý 
  Příběh Alvina Straighta není tak vstřícný a emocionálně jednoznačný jako Sloní muž z roku 1980. Může to znít trochu pohrdavě, 
  jako štemplování nálepkou „Lynch Light“, ale Sloní muž prostě patří k filmům, které se vlezou hlavně do škatulky „doporučeno s 
  dobrým svědomím, že nejde o pseudoumění ani kýč, ale prostě a jednoduše o silný příběh“.
  </p>
  <p>
  John Merrick se narodil ošklivě znetvořený a jeho podoba zavdala ke vzniku mýtu o tom, že do jeho matky, když byla těhotná narazil 
  slon a možná, že sám je synem slona. Léta pak žije jako pouťová příšera, zahalená v hadrech, než ho z tohoto otroctví vykoupí 
  lékař Frederick Treves, který jej hodlá zkoumat, léčit a učit žít normální život. Spádem okolností ovšem dojde k tomu, že přes 
  veškerou dobře míněnou snahu z Merricka udělá novou atrakci, a z barbarského stvoření děsícího lůzu vznikne „kultivované 
  monstrum“ obdivované společenskou smetánkou. Sloní muž Merrick je natolik inteligentní, že si svůj status uvědomuje a nakonec 
  spáchá slastnou sebevraždu, když ulehne ke spánku jako normální lidé a vadou svých nosních dutin se udusí.
  </p>
  <p>
  Film je natočen černobíle a expresionisticky zdůrazňuje hru světel a stínů, organicky včlení do vyprávění zamlženou snovou vizi 
  s běžícím slonem, a jakýsi „podzemní“ parní mechanizmus, pohánějící celý viktoriánský Londýn, v němž se celý příběh odehrává. 
  Herci s divadelní průpravou nevyčnívají z plátna, ale dovedou přirozeně přecházet mezi učenými vědeckými replikami a obyčejným 
  lidským projevem.
  </p>
  <p>
  Problém se Sloním mužem nastává pouze tehdy, když chceme vymezit jeho místo v Lynchově filmografii a vůbec v celém jeho 
  myšlenkovém světě. Možná vám to připadá jako zbytečnost. Když už se mi film jednou líbil, co na něm dál rozebírat a snovat 
  nějaké teorie okolo, že? Ono to ale má svůj důvod. Když totiž Sloního muže porovnáte s ostatními Lynchovými filmy, musíte se 
  zákonitě zeptat, jestli jde o výjimku, nebo zda zapadá do určité logiky a ideologie.
  </p>
  <p>
  Rozpětí Lynchovy tvorby je ukotveno mezi avantgardou (surrealizmem) a postmodernou – érou, v níž se avantgardní výboje staly 
  banální a umělci se snaží s touto banalitou všemožně vyrovnávat. Surrealizmus byl v meziválečném období minulého století 
  ovlivněn psychoanalýzou, která začala nahlížet člověka jako nemocný stroj ovládaný podvědomím a pudy. Rozum a bdělý 
  sebeuvědomělý stav tvoří minoritní složku naší osobnosti, většinu našich činů si neuvědomujeme. Osvícenecké vítězství 
  racionality se nemůže konat, nejvíce temnoty je totiž uzavřeno v lidské duši, aniž bychom mohli tuto temnotu jakkoli rozptýlit 
  světlem rozumu, jelikož její obrana a celková moc je příliš velká.
  </p>
  <p>
  Psychoanalýza v medicíně tak – nejspíš nechtěně – přispěla k tomu, že surrealizmus se postavil do opozice proti ideálům 
  humanizmu a jeho postupné kultivace člověka, který s postupujícím pokrokem nahlédne, jak budovat společnost co nejlépe a smíří 
  všechny myšlenkové směry. I další avantgardní proudy směřovaly k tomu, aby v nich bylo co nejméně člověka, co nejméně z 
  konvenčního (a lidsky omezeného) chápání reality. Soucit a dojetí se tu staly přežitkem, vítězí hrůza nad nezvladatelnou 
  lidskou podstatou. S takovouto ideovou výbavou se samozřejmě těžko dá vystavět klasický příběh a avantgarda tak nejenom 
  zobrazovala krizi civilizace, ale sama ji i prohloubila. Mazací hlava je možná nejčistší surrealistický film po Andaluském 
  psovi, a ani proklatá dávka černého humoru nás nedokáže zbavit deprese, která nás ovládne po jejím zhlédnutí.
  </p>
  <p>
  Do toho přichází po šedesátých letech, kdy se ještě věřilo na velkou změnu, skeptická postmoderna, která nevěří žádným „velkým 
  vyprávěním“, ani křesťanství, ani osvícenství, ani komunizmu, ani fašizmu, ani psychoanalýze. Všechno je obrovská fraška, 
  monumentální lež a konspirace. Věci přestaly mít podstatu, množství úhlů jak se na ně dívat je nekonečné a neexistuje jediný 
  správný pohled. To, co tu popisuji, jsou samozřejmě jen různé měnící se nálady doby a dá se s nimi polemizovat. Důležité v 
  našem případě je, jak se tyto nálady vepisují do uměleckých děl. Co vlastně můžeme ze Sloního muže vyčíst, když víme, že Lynch 
  ztělesňuje nejjasnější příklad postmoderního filmaře, který seknul s avantgardou a vydal se novými směry?
  </p>
  <p>
  Je Sloní muž v Lynchově filmografii čas oddechu, přechod od surrealizmu k postmoderně, nebo jisté tápání a nevědomost, kam se 
  přidat? Může být Sloní muž humanistický (a trochu romantický) proto, že jeho děj je zasazen do období konce devatenáctého 
  století, nepostiženého ještě zlomy, které se měly udát až ve století následujícím? Anebo to Lynch s tím humanizmem nemyslí tak 
  docela vážně? Natočil variaci na Truffautovo Divoké dítě (1966), román o výchově, nebo variaci na civilní horor Zrůdy (1932) 
  Toda Browninga o cirkusových mrzácích, který přenesl hrůzu z oblasti fantastiky do každodenního života? Není v jeho pojetí 
  Sloního muže i trocha výsměchu všem humanistickým filmům, které se berou stoprocentně vážně a staví prognózu lepšícího se 
  vývoje?
  </p>
  <p>
  Lynchův film obestřený mlhou, sevřený do stísněných prostorů a světelně zdůrazňující svoji umělost má daleko do realistických 
  románů, které i přes tragický konec kážou „tak to bylo dřív, ale dnes už bude líp“. Interpretace, chápající Sloního muže jako 
  jemnou disputaci o osvícenství, v níž proti idejím stojí filmový styl, dává dobrý smysl. Pokud by to Lynch s hlásáním 
  humanizmu myslel vážně, musel bych jít s hodnocením pořádně dolů.
  </p>
