  <ul>
  <li>Brr, běhal mi mráz po zádech... Něco tak depresivního se hned tak nevidí. Daleko "odpornější" čtyři minuty než celých 
  120 dnů Sodomy.
  <span class="uz">(May, www.csfd.cz)</span></li>
  <li>Výtvarne pojatá haluzná temnota, ideálny to večerníček pre vaše dieťa, pokiaľ mu chcete na celý život straumatizovať 
  abecedu a čokoľvek s ňou spojené.
  <span class="uz">(POMO, www.csfd.cz)</span></li>
  <li>ABECEDA = depresivní noční můra od A do Z. Jde o spojení animovaného a hraného filmu. Vše je velmi nezvykle, avantgardně 
  zpracované. Tento film skutečně není určen dětem! Dětem nepouštět, ani o něm zbytečně nevyprávět Poznámka: v hlavní a jediné 
  roli tohoto filmu se objevuje Lynchova první manželka - Peggy Lynch.
  <span class="uz">(Zputnik, www.csfd.cz)</span></li>
  <li>Názorná ukážka toho, do akej miery je filmové médium schopné pracovať s ľudskými emóciami a základnými pudmi, 
  znepokojivá kritika rigorózneho a otupujúceho vzdelávacieho systému a bolestne naliehavá spoveď režisérskeho podivína número 
  úno. Je skutočne fascinujúce sledovať rozmanité reakcie divákov na tento surrealistický kankán obrazu a zvuku - vnútorný 
  nepokoj, depresia, ohromenie, strach. Lynchova Abeceda je majstrovské dielo a pripomienka skutočnej sily 
  pohyblivých obrázkov.
  <span class="uz">(Melies, www.csfd.cz)</span></li>
  <li>Kdo dokáže za 4 minuty vytvořit takovou atmosféru a tak nehoráznou ujetárnu? Podle mě je jen jeden člověk, který je 
  tohoto schopen.. smekám mistře....:o) Jak jsem později zjistil, vše je podle snu neteře Lynchovy manželky. Lynch dokáže z 
  malého nápadu vymyslet něco tak hrůzostrašného, až to nahání husí kůži.
  <span class="uz">(Djkoma, www.csfd.cz)</span></li>
  <li>Abeceda z pohledu Davida Lynche kombinuje reálný a animovaný film, přičemž převládá ona kubistická animace. Obrazově 
  je film ponurý, naplněný sžíravou temnotou. Ta spolu s mistrovsky provedeným zvukovým doprovodem vytváří velmi depresivní 
  atmosféru, založenou na odporu vůči učení a pravidlům. Ať nám Lynch chtěl tímto snímkem říci cokoliv, přehnal to. Nicméně 
  dokázal, že se opravdu umí dotknout temných myšlenek diváků a zneužít je na pouhých necelých čtyřech minutách k rozpoutání 
  vtíravé krvavé melancholie.
  <span class="uz">(Eodeon, www.csfd.cz)</span></li>
  <li>Stejný styl animace jako u "Six men getting sick" ještě s děsivějším dějem. Dětský hlasy spolu s mužským (operním) 
  vytváří příjemnou náladu, ale hned začně tzv. knučení štěněte a to ve mě vyvolalo nepředstavitelnou tíseň, které po chvíli 
  přestane. Už s úlevou si oddechuji. Lynch mě nenechal ani si odpočinout a už je tu další zvuk... Režisér tohoto animovaného 
  "kraťasu" si vás vychutná až do poslední scény.
  <span class="uz">(Kryšpín, www.csfd.cz)</span></li>
  </ul>
