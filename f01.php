  je oceněn to jde. To mě se nepodařilo :o( To dokazuje, že Lynch má od počátku geniální nápady, které díky své šikovnosti dokáže převést 
  na plátno. A to si zaslouží potlesk. Režisér jako on se narodí jednou za sto let.
  <span class="uz">(Kryšpín, www.csfd.cz)</span></li>
