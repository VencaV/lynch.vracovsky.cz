  <p class="bezti">* 20. ledna 1946, Missoula, Montana</p> 
  <p>David Keith Lynch je Americký filmový scenárista a režisér, pravděpodobně nejvýznamnější představitel postmoderní 
  kinematografie. Ve svých filmech se zabývá lidskou psychikou, kdy se snaží zobrazit její horší stránky. 
  Jeho filmy jsou považovány za provokativní a obsahují značné množství propracovaných vizuálních efektů.
  Vedle hlavní <a href="filmy1.php" title="Přehled filmové tvorby Davida Lynche">filmové tvorby</a>
  tvoří i <a href="filmy2.php" title="Přehled krátkých filmů Davida Lynche">krátké filmy</a>; režíroval 
  <a href="filmy3.php" title="Přehled televizní tvorby Davida Lynche">televizní filmy a seriály</a>
  i <a href="filmy4.php" title="Přehled dokumentární tvorby Davida Lynche">dokumentární filmy</a>.
  V několika (nejen) svých filmech si zahrál, je i filmovým producentem. David Lynch je i hudební skladatel,
  textař, fotograf, malíř a výtvarník. Jeho výtvarná díla jsou ovlivněna expresionismem.</p>
  <h2>Biografie</h2>
  <p>
  Místo své biografie rád uvádí jen skutečnost, že v rodném městečku Missoula v Montaně dosáhl skautského titulu
  Eagle Scout (špeh s orlíma očima). Bezpečná maloměstská atmosféra, v níž prožil bezproblémově šťastné dětství, 
  se stala jedním z východisek jeho pozdější temně surrealistické obraznosti. Už jako dítě toužil vědět, co se 
  skrývá za úsměvy dospělých a bělostným lakovým plotem obrostlým rudými růžemi (jak jej ukázal ve filmu Modrý 
  samet). Touha pronikat do temných tajemství reality spolu s přesvědčením, že svět je ve své podstatě 
  nepoznatelný, se později staly hlavními analytickými nástroji autora, který s jejich pomocí proniká pod povrch 
  naivního žánrového světa amerického snu. Další zdroj, z nějž David Lynch bohatě čerpá, je spojen s malířským vnímáním 
  světa: vystudoval výtvarné umění a malování dodnes patří k jeho zálibám. Na škole se začal věnovat také filmu.
  </p>
  <p>Středoškolské vzdělání získal v Alexandrii, poté studoval malířství v Bostonu. Roku 1965 zahájil studia 
  na Pennsylvania Acadamy of Fine Arts, která ukončil roku 1969. Zde vytvořil svůj první animovaný film 
  <a href="f01.php" title="Six Figures Getting Sick">Six Figures Getting Sick</a> (1967),
  po něm i druhý krátký film <a href="f02.php" title="The Alphabet">The Alphabet</a> (Abeceda, 1968). 
  Roku 1967 se David Lynch oženil s Margaret (Peggy) Reavey a v dubnu 1968 se mu narodila dcera Jennifer.
  Ta se později proslavila svým filmem Boxing Helena (Helena v krabici) - o ženě, jíž posedlý a zároveň šíleně 
  zamilovaný doktor postupně zbavuje končetin, aby ji nakonec mohl mít jen pro sebe. Napsala také knihu Tajný 
  deník Laury Palmerové, obsahující zápisky postavy, jejíž smrtí začíná kultovní seriál Twin Peaks od jejích 
  dvanácti do sedmnácti let.</p>
  <p>
  Roku 1970 natočil David Lynch další krátký film <a href="f03.php" title="The Grandmother">The Grandmother</a> (Babička, 1970).
  Téhož roku se se svojí ženou Peggy a dcerou Jennifer přestěhoval do Los Angeles. První experimentální krátké snímky 
  kombinující animované a hrané postupy mu otevřely cestu k hrané tvorbě. Studoval na čerstvě otevřeném Centru 
  pro pokročilá filmová studia při Americkém filmovém institutu (AFI), kde roku 1971 zahájil práci na svém 
  celovečerním debutu – osobité černobílé vizi <a href="f04.php" title="Eraserhead">Eraserhead</a> (Mazací 
  hlava, 1976).
  </p>
  <p>
  Roku 1974, v průběhu rozvleklého natáčení Eraserhead, vytvořil David Lynch další krátký snímek <a href="f05.php" title="The Amputee">
  The Amputee</a> (1974); téhož roku se rozvedl se svou ženou Peggy.
  Roku 1976 dokončil film <a href="f04.php" title="Eraserhead">Eraserhead</a> (Mazací hlava, 1976). Toto dílo mělo pro Lynche 
  zvláštní význam, práce na něm mu zabrala takřka 6 let a odráží se v něm jeho pocity a vzpomínky. V době 
  práce na tomto filmu pracoval pod velkým tlakem a potýkal se s nedostatkem peněz, také se musel starat o rodinu. 
  Člověk v tomto snímku může vypozorovat mnoho ze soukromého života Davida Lynche.
  </p>
  <p>
  Díky kultovnímu úspěchu tohoto snímku mu v roce 1979 nabídl Mel Brooks služby své produkční společnosti 
  Brooksfilms a pod její výrobní záštitou realizoval černobílé „viktoriánské“ drama <a href="f06.php" 
  title="The Elephant Man">The Elephant Man</a> (Sloní muž, 1980). I s ním David Lynch sklidil velký úspěch, 
  hlavně komerční, což mu zajistilo dobré jméno a ukázalo jeho cit pro filmování (Elephant Man byl nominován 
  na 8 Oscarů).
  </p>
  <p>
  Zklamáním se stal jeho střet s hollywoodskou továrnou na sny – výpravná sci–fi inspirovaná kultovním románem 
  Franka Herberta <a href="f07.php" title="Dune">Dune</a> (Duna, 1984). Roku 1981 Davidu Lynchovi nabídl producent 
  Dino De Laurentiis zfilmování tohoto vědecko-fantastického románu. David Lynch, podle jeho slov, neměl 
  zvláštní zálibu ve sci-fi, ale to se změnilo po přečtení knižní novely. Film byl sestříhán ve dvou verzích: 
  dvouhodinové, určené pro kina a čtyřhodinové televizní montáži. Snímek bohužel neuspěl. Podle kritiky za to 
  mohl složitý děj a větší náročnost na diváka. Možná právě tento neúspěch Davida Lynche svedl zpět na cestu, kterou 
  si již dříve vytyčil.
  </p>
  <p>
  Mezi přední autory americké filmové postmoderny se zařadil umělecky nekompromisním snímkem <a href="f08.php" 
  title="Blue Velvet">Blue Velvet</a> (Modrý samet, 1986), na kterém pracoval opět s producentem Dinem De 
  Laurentiis. Film zaznamenal už daleko větší úspěch než Dune. I když ne tak docela, zpočátku nebyl snímek 
  dobře přijímán, ale jakmile se o něm jistí kritici začali více zmiňovat, ukázala se postupně jeho pravá 
  kvalita, skrytá za, pro mnohé již dobře známým Lynchovým uměním. Sám David Lynch řadí tento film mezi ty, které 
  mu jsou nejblíže. Blue Velvet získal řadu ocenění, byl nominován také na Oscara a právem se řadí k tomu 
  nejlepšímu, co Lynch natočil. Při jeho natáčení se poprvé setkal se skladatelem Angelem Badalamentim, 
  který pro něj složil hudbu, s Julee Cruise, která pro něj nazpívala skladbu Mysteries Of Love a také se svou 
  pozdější životní partnerkou Isabellou Rossellini.
  </p>
  <p>
  Roku 1988 natáčí David Lynch pro Francouzskou televizi dvaadvacetiminutový příspěvek do složeného filmu několika 
  režisérů <a href="f09.php" title="Les Français vus par...">Les&nbsp;Français vus par...</a> (Francouzi očima..., 1988). 
  Jeho samostatný snímek nese název The Cowboy and the Frenchman.
  </p>
  <p>
  V roce 1989 Lynch produkuje s Badalamentim album Floating Into the Night, které také otextoval. Tento rok začíná práce na 
  <a href="f10.php" title="Twin Peaks - seriál">Twin Peaks</a> (1989 - 1991), a to natočením pilotního filmu pro televizi, 
  který vznikl za spolupráce s Markem Frostem.
  </p>
  <p>
  Roku 1990 vzniká první řada seriálu <a href="f10.php" title="Twin Peaks - seriál">Twin Peaks</a>. Některé epizody, včetně 
  pilotu režíroval sám David Lynch. Tento seriál získal kultovní věhlas. I když se zabývá vážným tématem, najdeme v 
  něm prvky černé komedie, fantasy a mnoho situací, které člověka oslovují tak, že neví jestli se má bát, být 
  smutný, či smát se. Ve stejném roce vznikají ještě televizní muzikál <a href="f11.php" 
  title="Industrial Symphony No. 1: The Dream of the Broken Hearted">Industrial Symphony No. 1: The Dream of the Broken Hearted</a>,
  do kterého vtělil svůj zájem o hudbu; a roadmovie <a href="f12.php" title="Wild at Heart">Wild at Heart</a> 
  (Zběsilost v srdci, 1990), se kterým získal mnoho ocenění včetně Zlaté palmy v Cannes. Film je založen na 
  literární předloze Barryho Gifforda. V témže roce režíroval ještě dokumentární seriál <a href="f13.php" 
  title="American Chronicles">American Chronicles</a> (1990).
  </p>
  <p>
  V letech 1991 - 1992 Lynch pracuje na televizním seriálu <a href="f14.php" title="On The Air">On The Air</a> (1991-1992), který však neuspěl. 
  Následuje celovečerní film <a href="f15.php" title="Twin Peaks: Fire Walk With Me">Twin Peaks: Fire Walk With Me</a> 
  (Twin Peaks: Ohni se mnou pojď, 1992).
  V něm se režisér vrací do Twin Peaks, aby vyprávěl o posledních dnech Laury Palmerové. Film má místy silnou atmosféru a 
  znepokojující děj, který člověka někdy až vyvádí z poklidu, ale je mu občas vytýkáno příliš úzké dějové zaměření. 
  Stejně jako např. filmu <a href="f12.php" title="Wild at Heart">Wild at Heart</a>, kde se někomu nelíbí přílišná kýčovitost. 
  Ale malý ohlas u Twin Peaks: Fire Walk With Me je možná způsoben i tím, že se poměrně hodně liší od seriálu a je to možná 
  "něco trochu jiného" než by divák mohl očekávat. I v jeho osobním životě nastává změna, narodí se mu druhý syn Riley. Jeho 
  matkou je Mary Sweeney, Lynchova dlouholetá spolupracovnice a přítelkyně.
  </p>
  <p>
  Od roku 1992 následuje útlum, ve kterém David Lynch režíruje např. televizní trilogii <a href="f16.php" 
  title="Hotel Room">Hotel Room</a> (1993). Stal se jedním ze čtyřiceti režisérů, kteří 
  oslavili století kinematografie ve filmu <a href="f17.php" title="Lumiere and Company">Lumiere and Company</a> (Lumiér a spol., 1995),
  který byl filmován původní kamerou bratří Lumiérů. Zkouší více malovat a vydává další hudební album společně s Angelem 
  Badalamentim a Julee Cruise - The Voice of Love. Ujímá se produkce upírského filmu Nadja a snímku Crumb.
  </p>
  <p>
  S rokem 1997 přichází film <a href="f18.php" title="Lost Highway">Lost Highway</a> (1997). Stejně jako v 
  případě Wild at Heart na něm spolupracuje s Barry Giffordem. Snímek je považován za synonymum ke stylu film 
  noir a je i tímto považován za klasiku. Tento rok založil Lynch produkční společnost Picture Factory, 
  společně s Mary Sweeney a Nealem Edelsteinem.
  </p>
  <p>
  Roku 1999 začíná pracovat na pilotním filmu k seriálu Mulholland Drive a také začíná natáčet další 
  celovečerní film <a href="f19.php" title="The Straight Story">The Straight Story</a> (Příběh Alvina 
  Straighta, 1999). Tento krásný příběh, který je založen na skutečné události, se sice vymyká jeho obvyklé tvorbě,
  ale přesto patří k jeho nejlepším snímkům. Roku 2000 David Lynch začíná vytvářet (jen pro internet) kreslené 
  seriály ve Flashi jménem <a href="f20.php" title="Dumbland">Dumbland</a> (2002).
  </p>
  <p>
  <img class="right" src="img/lynch2.jpg" alt="David Keith Lynch" height="248" width="296">
  V roce 2001 má premiéru celovečerní film <a href="f21.php" title="The Mulholland Drive">The Mulholland Drive</a> (2001). 
  Tento název měl původně patřit novému seriálu pro společnost ABC, ale po neshodách a neochotě podřídit se 
  zásahům ze strany ABC Lynch materiál přepracoval a dotočil s francouzskou koprodukcí jako celovečerní film, 
  který také získal řadu ocenění a řadí se k nejlepším snímkům Davida Lynche.
  </p>
  <p>
  V roce 2002 vznikly krátké snímky <a href="f22.php" title="Darkened Room">Darkened Room</a> (2002), 
  <a href="f23.php" title="Rabbits">Rabbits</a> (2002) a <a href="f20.php" title="Dumbland">Dumbland</a> (2002);
  a dokumentární videofilm <a href="f24.php" title="The Short Films of David Lynch">The Short Films of David Lynch</a> 
  (Krátké filmy Davida Lynche, 2002).
  </p>
  <p>
  Zatím posledním dílem Davida Lynche je film <a href="f25.php" title="Inland Empire">Inland Empire</a> (2006), 
  který měl u nás premiéru 12.7. 2007.
  </p>
  
