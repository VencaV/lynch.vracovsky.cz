  <h2 id="zal1">Obsah</h2>
  <p>
  Průzračně jednoduché vyprávění inspirované skutečným příběhem vypráví o třiasedmdesátiletém muži(Richard Farnsworth), 
  který se vypravil na obstarožním zahradním traktůrku na cestu dlouhou 300 mil. Jeho bratra totiž postihl záchvat mrtvice 
  a Alvin cítí, že by ho po letech, kdy spolu nemluvili, měl navštívit. Během své cesty potkává nejrůznější lidi a prožívá 
  nejrůznější drobné události... Minimalistická road movie odvíjející se rychlostí sedm kilometrů za hodinu spřádá „malý“ 
  příběh do podoby „velkého“ vyprávění o svobodě, vůli a pokání – bez brutality, temných stránek duše a dokonce bez 
  neslušných slov.
  </p>
