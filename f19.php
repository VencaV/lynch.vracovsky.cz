  <h2 id="zal2">Recenze</h2>
  <div class="citace">
  <b>Motto:</b> Je to docela normální film. (David Lynch)
  </div>
  <p>
  David Lynch je bezesporu jedním z nejzajímavějších soudobých režisérů. Jeho filmy jsou snově temné a obvykle se nedají nijak 
  jednoduše vyložit. Podle skutečné události natočený Příběh Alvina Straighta je ale snad ve všech ohledech naprostým opakem jeho 
  předešlé tvorby. Jedni to nejspíš budou považovat za zradu "lynchovských" ideálů, jiní za přirozený vývoj umělce a další si 
  budou myslet, že si z nás Lynch prostě dělá legraci. Mě osobně je to ale úplně jedno, protože Příběh Alvina Straighta je 
  především výborný film a už dlouho jsem z kina nevycházel v tak příjemné náladě.
  </p>
  <p>
  Alvin Straight je sedmdesátiletý stařík, který žije se svou mírně retardovanou dcerou Rose ve městečku Laurens v Iowě. Zdraví 
  už mu moc neslouží, jednotlivé orgány postupně vypovídají službu, ale v hlavě to má Alvin uspořádané lépe než drtivá většina 
  mladých a zdravých. Když se jednoho dne dozví, že jeho bratr Lyle, se kterým už několik let nepromluvil ani slovo, dostal 
  mrtvici, rozhodne se ho navštívit. Za svůj zahradní traktůrek značky John Deere zapojí provizorní přívěs a vydá se na šest 
  týdnů dlouhou cestu do 300 mil vzdáleného Mt. Zionu v sousedním Wisconsinu.
  </p>
  <p>
  Na své cestě Alvin potkává celou řadu lidí, představujících nejrůznější vrstvy maloměstské Ameriky, jejichž osudy se s tím 
  jeho na krátký okamžik protnou, aby se po chvíli zase rozešly každý svým směrem. Alvin je sice tou postavou, která je všechny 
  spojuje, ale film je ve skutečnosti právě tak o něm, jako o lidech, které cestou potkává. Jednotlivá zastavení slouží k tomu, 
  abychom se dozvěděli něco víc o Alvinovi a jeho minulosti, a Lynch se nám tak snaží nenásilným způsobem přetlumočit onu starou, 
  známou a banálně znějící pravdu, totiž že i cesta může být cílem.
  </p>
  <p>
  Příběhem Alvina Straighta Lynch dokazuje, že je především skvělý režisér, který umí vyprávět prostý a přímočarý příběh i bez 
  oněch formálních obezliček, které jeho styl tak proslavily. Každý záběr je perfektně zkomponován a prozrazuje pevnou režijní 
  ruku. Film se odvíjí pomalu, jeho tempo přesně odpovídá rychlosti Alvinova traktůrku, a Lynch dokáže udělat dramatickou situaci 
  i z toho, jestli se Alvinovi s jeho vozidlem podaří zajet do stodoly včas, aby nezmokl. Není se tedy co divit, že jediná 
  částečně "lynchovská" scéna do filmu poněkud nezapadá a působí lehce nesourodě.
  </p>
  <p>
  Richard Farnsworth podává v roli Alvina Straighta svůj životní herecký výkon. Mluví pomalu, pečlivě váží každé slovo, a také 
  proto jeho řeč zní tak přesvědčivě. Alvin není žádný velký intelektuál, ale v dobrém slova smyslu prostý a obyčejný člověk. 
  Jeho mluva není plná učených obratů, ale oné přirozené moudrosti a nadhledu, které se dají získat pouze stářím, a i tak se to 
  povede pouze těm šťastnějším z nás. Některé jeho promluvy sice vyznívají až příliš idylicky, jako když se mu podaří přesvědčit 
  mladou těhotnou stopařku, která utekla z domova, že rodina je nade vše, ale podobných míst naštěstí moc není.
  </p>
  <p>
  Fanatické příznivce rychlé akce by Příběh Alvina Straighta unudil k smrti po několika minutách, protože z jejich pohledu se v 
  něm prakticky nic neděje. Pokud se ale umíte na chvíli zastavit a vychutnat si pomalý způsob vyprávění, pak je tohle film 
  přesně pro vás. Vynalézavá kamera Freddie Francise a podmanivá hudba Lynchova dvorního skladatele Angela Badalamentiho 
  vytvářejí dohromady velmi poutavý zážitek. Vlnící se lány obilí a stromy obalené listím dokonale evokují náladu končícího léta, 
  kdy není nic lepšího než si sbalit batoh a spacák, vytáhnout z garáže kolo (nebo traktůrek) a vydat se na cestu.
  </p>
  <p>
  Příběh Alvina Straighta ale rozhodně není klasicky radostná road-movie. Pamatujete si vzrušení, s jakým jste se poprvé vydávali 
  na nějakou delší cestu? Pamatujete si, s jakým nadšeným očekáváním jste vyhlíželi z okna autobusu a doslova hltali ubíhající 
  krajinu? A pamatujete si taky, jak na vás působila cesta zpátky? Projížděli jste stejnými místy, ale stromy mezi tím už trošku 
  opadaly, a vy jste byli zase o pár týdnů starší. Do pocitů radosti z toho, že se vracíte domů, se mísily nostalgické vzpomínky 
  na to, co necháváte za sebou. A přesně takovým dojmem působí Přiběh Alvina Straighta.
  </p>
  <p>
  Twin Peaks jsem půl roku nadšeně hltal každý týden na ČT2. U Modrého sametu a Zběsilosti v srdci jsem se chvílemi bavil a 
  chvílemi nudil. Lost Higway jsem sice nepochopil, ale tvářil jsem se, že je mi to jedno. Mazací hlavu jsem viděl několikrát a 
  vždycky na mě silně zapůsobila, ale na otázku, jestli se mi opravdu líbila, bych odpovědět nedovedl. O Příběhu Alvina Straighta 
  to můžu říct s klidným svědomím, neboť ještě dlouho po jeho konci jsem měl na tváři takový ten přiblblý úsměv člověka, který 
  právě zažil něco tak příjemného, že tomu částečně nechce věřit a částečně se bojí, že se mu to jenom zdálo. 
  </p>
