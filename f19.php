  <h2 id="zal3">Z diskuzí...</h2>
  <ul>
  <li>Skvělý výkon Richarda Farnswortha, vynikající muzika a "klasicky Lynchovská" režie dělají z tohoto jednoduchého lidského 
  příběhu nejlepší "roadmovie", kterou jsem měl to potěšení vidět.
  <span class="uz">(Golfista, www.csfd.cz)</span></li>
  <li>Opravdu velmi netradiční a přímočará lynchovka se sebeparodickými prvky a hřejivou atmosférou a nádherný příběh o 
  znovunalezeném bratrství, které i přes svou banalitu v lynchově podání vyznívá o stupínek dva důležitěji. Po tomhle filmu si 
  pár věcí uvědomíte...
  <span class="uz">(kOCOUR, www.csfd.cz)</span></li>
  <li>Jen o něco málo víc než příjemný filmeček o stáří. Ale Richard Farnsworth opravdu umí. Nejvíce se mi líbily záběry, kdy 
  kamera zabírá pole a podmalovává to nádherná hudba.
  <span class="uz">(Rob Roy, www.csfd.cz)</span></li>
  <li>Dosti netypický Lynch, alespoň co se povrchového doteku týká... Jímavý příběh o tvrdohlavém dědulovi, který se jednoho 
  krásného dne sebere, popadne traktůrek a jede dlouhý lán světa usmířit se se svým bratrem. Typická forma road movie v Lynchově 
  podání nenudí ani trochu, sled poetických a vtipných historek má obrovskou vnitřní sílu a bodré moudro starého a samorostlého 
  staříka, kterého skvěle zahrál Richard Farnsworth. Nechybí ale ani tradiční "lynchovinky" (viz scéna se srnami). Příjemné, 
  hladivé, poetické. Krásný snímek.
  <span class="uz">(Marigold, www.csfd.cz)</span></li>
  <li>Skutečně krásný hřejivě lidský příběh v režii muže, od kterého bychom to opravdu nečekali. Žádný přehnaný sentiment, jen 
  dojemná story o cestě za usmířením.
  <span class="uz">(Eda S, www.csfd.cz)</span></li>
  <li>Milý a potěšující film s výborným Richardem Farnsworthem v hlavní roli. S radostí jsem díky tomu mohl zjistit, že Lynch 
  umí točit nejen dokonalé "psychárny", ale zvládne i hřejivý lidský příběh.
  <span class="uz">(Radyo, www.csfd.cz)</span></li>
  </ul>
