  <h2 id="zal2">Recenze</h2>
  <p>
  „Svět je v jádru zběsilý a navenek prapodivný,“ konstatuje ústřední protagonistka zvrhlé love story Zběsilost 
  v srdci, kterou natočil David Lynch podle stejnojmenné novely Barryho Gifforda (1984). Shrnuje tak podstatu 
  nejen tohoto filmu, ale celé Lynchovy tvorby, která byla v případě road movie o perverzně nevinné milenecké 
  dvojici padlých andělů na útěku před zákonem i pekelnými nástrahami pozemského světa, korunována Zlatou 
  palmou v Cannes.
  <p>
  <h3>Elvis a Marilyn ze země Oz</h3>
  <p>Režisér Giffordovu útlou předlohu propojil s ohnivě-krvavou ozvěnou fantazijní pohádky Čaroděj ze země Oz 
  (opakující se odkazy na její postavy, červené střevíčky či křišťálovou kouli). Rozehrává přitom apokalyptický 
  příběh o (post)moderním Elvisovi a novodobé Marilyn, které před hrůzami a krutostmi tohoto světa dokáže 
  ochránit jen jejich láska – bez ohledu na to, že se v jejím jménu brutálně vraždí. Oním milujícím zabijákem 
  je do saka z hadí kůže oblečený Námořník (Nicolas Cage), jenž po propuštění z vězení (kde si odpykával trest za 
  zabití) jede „za duhou“ s naivně romantickou milenkou Lulou (Laura Dern). Po anonymních silnicích a motelech 
  je však sledují zabijáci, najatí dívčinou mstivou matkou (Diane Ladd je skutečná matka Laury Dern).
  </p>
  <p>
  <img class="right" src="img/f12/02.jpg" alt="Nicolas Cage a Laura Dern" height="230" width="320">
  Gifford nebyl s Lynchovým (sebe)parodickým převodem příliš spokojen (navzdory tomu se ale o šest let později 
  oba tvůrci sešli při psaní scénáře k snímku Lost Highway). Knížka Lynchovi posloužila jen jako základní 
  synopse, na níž nechal vzplanout svůj svět ohně a krve. Jako všichni Lynchovi hrdinové i Námořník musí projít 
  peklem, aby poznal, že se nesmí odvracet od své lásky a že musí změnit život „dřív, než bude tancovat bez 
  hudby v oprátce“. Lynchovy filmy jsou vystavěny na principu polarizace (andělé–démoni, panna–děvka atd.), 
  přičemž v tomto snímku jsou tyto kontrasty nejextrémnější. Zběsilost v Námořníkově srdci se ventiluje 
  bestiálním násilím, vzápětí se ale své milované vyznává elvisovskou láskyplnou písní (Cage i Dern tyto jímavé 
  scény ale hrají na hraně afektované karikatury), kýčovité západy slunce střídají brokovnicí odstřelené hlavy. 
  S protikladnými výjevy koresponduje i hudba, jež vedle sebe staví hladivé melodie Angela Badalamentiho, 
  oplodňovací ploužáky Chrise Isaaka, swingové rytmy Glenna Millera i brutální riffy trashmetalové kytary 
  skupiny Powermad.
  </p>
  <p>Zběsilost v srdci sice triumfovala v Cannes, není to ale mistrovské dílo, jako Modrý samet – byť i tady 
  najdeme silná místa, jakým je setkání dvojice s dívkou, jež jim po bouračce zemře před očima (hraje ji 
  Sherilyn Fenn, představitelka „poupátka“ Audrey z Městečka Twin Peaks). Lynchova „braková“ estetizace 
  násilí – navzdory snaze zmírnit ji škodolibě pokřivenými gagy – místy narůstá do odpudivě naturalistických 
  rozměrů (mozek vytékající z hlavy), vedlejší motivy zůstávají jen v náznaku (a jeví se zbytečné – viz zvrhlá 
  rituální poprava detektiva) a řada výstředních figurek se jen krátce objeví a zase zmizí, takže jejich 
  účinkování působí spíše jako samoúčelně matoucí naschvál (invalida, jenž způsobí havárii, postavy starých 
  lidí ...). Na roztříštěnosti filmu se podepsal drastický sestřih: z původních dvě stě sedmdesáti minut 
  skončila více než polovina pod střihačským pultem. Přestože leitmotivem filmu je oheň coby symbol všeničícího 
  nebezpečí, skutečný žár Zběsilost v srdci často postrádá.</p>
