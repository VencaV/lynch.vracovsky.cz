  <h2 id="zal3">Z diskuzí...</h2>
  <ul>
  <li>Další klasická lynchovina o lidech, kteří si prožijou svoje osobní peklo, před nímž je spasí až útěk do sladce 
  kýčovitého klimbu. V tomhle si jsou Mazací hlava, Modrý samet a Zběsilost v srdci hrozně podobné - a přece díky 
  Lynchově režijní invenci a nevysychající studnici jeho soukromých obscesí a nočních můr tolik rozdílné. Tehle 
  film není jenom o explodující hlavě...
  <span class="uz">(Betelgeuse, www.csfd.cz)</span></li>
  <li>Další zvláštní David Lynch, který mi nesedl tak jak bych si sám představoval. Nicolase Cage miluju i v 
  tomto snímku a je jen škoda, že se k němu moc herců nepřidalo. William Dafoe je dábelsky nechutný, ale Laura 
  Dern je zvláštní. Něco tomu filmu chybí. Kus Lynche? Možná, ale rozhodně nějaký silný moment, který by ho 
  učinil nesmrtelným stejně jako jiné Lynchovy filmy.
  <span class="uz">(Djkoma, www.csfd.cz)</span></li>
  <li>Již po několika minutách lze zcela jistě určit Lynchův charakteristický rukopis. Spousta mlžení, temné 
  budování atmosféry s patřičně vybičovanou psychikou hlavních postav (jak záporných tak kladných), občasná 
  nelogičnost se spoustou symbolů, amorálnost světa a mnoho jiného. Tato témata, stejně jako spousta dalších, 
  cyklicky proplouvají většinou Lynchových filmů a přesto nezavání stereotypem, i když... V případě Zběsilosti 
  v srdci se již o jistém stereotypu dá mluvit. Typické režisérovi figurky se mění v kýčovitou vrstvu 
  neobvyklých povah a fyzických disfunkcí (jednonohá vražedkyně, Peru a jeho vyviklané zuby). I lehká symbolika 
  tu jaksi nejde dohromady se syrovou estetikou. Zůstává jen obyčejný příběh v oduševnělém kabátku. Lynch buď 
  stárne (případně se u něj projevuje sentimentální akcent) nebo se mu nedostává nápadů.
  <span class="uz">(dwi, www.csfd.cz)</span></li>
  <li>První film, který jsem od Davida Lynche kdy viděl. Šel jsem na to 3x, a pokaždé mě velice bavilo sledovat 
  postupně se vyprázdňující kino. Společně s Blue Velvet Lynchův nejlepší kousek. Doporučuji těm, kdo mají 
  rádi svérázné rebelské hrdiny a rock´n´roll, nevadí jim trocha drsné brutality a bláznivé romantiky. K tomu 
  je třeba samozřejmě připočítat i typicky lynchovské obskurnosti a jeho fascinaci temnotou. "Zběsilost" 
  pochází ještě z dob, kdy si Lynch se svými filmy věděl rady a nepotřeboval je v polovině měnit v (sice 
  zajímavě surreálné, ale) málokomu srozumitelné orgie.
  <span class="uz">(Mr.Apache, www.csfd.cz)</span></li>
  <li>David Lynch v žánrové směsici, podávané s brutální syrovostí i ironickým romantickým nadhledem. 
  Po Městečku Twin Peaks pro mě Lynchův nejlepší opus. Doporučeno pouze vícevrstevnatým lidem, kteří realitu 
  neberou roboticky racionálně a vědí, že šílenství není nemoc, ale nadstavba. Šedé myšky u toho musí pištět 
  hrůzou a nevolností:-).
  <span class="uz">(Altamir, www.csfd.cz)</span></li>
  </ul>
