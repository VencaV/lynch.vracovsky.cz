  <h2 id="zal1">Obsah</h2>
  <p>
  Kriminálník Sailor (Nicolas Cage) a půvabná Lula (Laura Dern) se milují navzdory šílenému světu, který je 
  obklopuje. Na útěku před spravedlností a Lulinou zuřící matkou se pokoušejí zachránit svou lásku – což se jim 
  nakonec povede, takže Sailor může své milované zazpívat elvisovku „Love Me Tender“. Mix klasické road 
  movie a Čaroděje ze země Oz režisér zázračně očistil od banality nejkřiklavějších vypravěčských klišé. 
  Zlatá	palma na MFF v Cannes na jedné straně a hluboké pobouření části kritiků na straně druhé pomohlo 
  zformovat	nespravedlivou pověst Lynche jako úmyslně	skandálního autora.</p>
