  <h2 id="zal2">Z diskuzí...</h2>
  <ul>
  <li>Další, v pořadí již třetí Lynchův filmový počin. Opět jde o dílo velmi surrealisticky zpracované. Snímek pojednává o malém 
  chlapci, který zažívá mnoho trápení, týrání a příkoří. Lásku, útěchu a radost ze života nachází u své (metamorfózou vzniklé) 
  babičky. Velmi silný snímek o tom, že "dotek" může bolet a zároveň může (a měl by) být projevem krásných citů. Film, u něhož 
  jsem si několikrát vzpomněl na ono "beatlesovské": "All we need is love". ;o) Poznámka: BABIČKA je první Lynchův počin, o němž 
  se dá říct, že se ze škatulky Filmový projekt přesunul do škatulky Film. David Lynch na tento svůj opus dostal grant od The 
  American Film Institute. Splnil totiž dvě podmínky pro obdržení grantu: 1. existence nejméně dvou projektů (Lynch v té době 
  měl dva vytvořené projekty - SIX FIGURES GETTING SICK a THE ALPHABET) a 2. předložení projektu, na který měl být grant 
  udělen (BABIČKA).
  <span class="uz">(Zputnik, www.csfd.cz)</span></li>
  <li>Lynch geniálně přešel od animovaného ke hranému filmu. Je to takový surrealistický horor, skvělé zfilmování děsivého snu.
  <span class="uz">(kinej, www.csfd.cz)</span></li>
  <li>Stále velice špatné a nicneříkající... Dílko je už ale trochu sympatičtější, než v předešlých dvou "filmech", atmosféra je 
  vytvořena za autorsky profesionálnějších metod, než neustále řvoucí sirénou, nebo vykřikováním hlásek, vyskytují se dokonce i 
  náznaky příběhu a ve třetí čtvrtině se objeví vynikající hudba. To je ale tak vše.
  <span class="uz">(Dan9K, www.csfd.cz)</span></li>
  <li>Sám príbeh vlastne nie je nijak šokujúci ani desivý, je skôr silný a citlivý, pojednáva o potrebe lásky, rodiny a vzájomného 
  pochopenia, a viem si dosť dobre predstaviť ako milo (ale tiež zdravo ujeto) by ho interpretoval napríklad taký Tim Burton za 
  pomoci hudby Dannyho Elfmana. Ale toto nie je film Burtona, je to číra lynchovina, takže tu máme pôsobivo mrazivú atmosféru, 
  neľudsky vyzerajúcich, odstrašujúcich hercov s tvárami bielymi ako stena, krvavo červenými perami a očami a psychopatickými 
  pohľadmi, ďalej krásne štýlové prechody od animovaného k hranému, od čiernobieleho k farbám (trochu mi to pripomína neskorší 
  comics Sin City, ktorý je tiež čiernobiely a a farby sa tam vyskytujú len zriedka, ale vtedy sú sýte a vždy niečo zdôrazňujú) 
  a nakoniec bláznivé psychedelické zvuky, ktoré sem-tam prechádzajú do niečoho, čo sa dá nazvať hudba. Ako celok je to mimoriadne 
  pôsobivé, Lynch skvelo ilustroval svoj nekonečný talent a formálnu originalitu už dávno predtým, ako si ho niekto začal všímať.
  <span class="uz">(Tommy987, www.csfd.cz)</span></li>
  <li>Sice jednoduchý příběh (není se co divit vzhledem k délce), ale to zpracování je nezapomenutelné, vyjímečné, děsivé, šokující. 
  Okamžitě poznáte, že jde o lynchovinu.
  <span class="uz">(ladylucifer, www.csfd.cz)</span></li>
  <li>Geniálny nápad (chapec si zasadí vlastnú babičku:)) pretavený do ďalšieho surealistického znepokojivo-pochmúrneho dielka, ktoré 
  mi príde najmä po vizálnej stránke ako dokonalejšie pokračovanie Abecedy. Ako sám Lynch o Grandmother vraví: "Bolo to nahustené 
  bez poriadneho skutočného scenára"...Asi ma Lynchovy fanúšikovia "zlynčujú", ale nedalo by sa toto tvrdenie aplikovať aj na 
  také Lost Highway?
  <span class="uz">(jojinecko, www.csfd.cz)</span></li>
  <li>V mých očích je babička nejlepší krátký psycho od Lynche. Lepší už to snad ani bejt nemůže. Lynch piloval svůj jedinečný styl 
  už od mládí, a babička to jen potvrzuje, je to jeho snad vůbec první delší film, který natočil a příběh o malém klukovi, který 
  má zlé rodiče a je nešťastný, ale jeho záchranou je jeho babička, která ho jako jediná má ráda, ale umírá. Film nemá absolutně 
  žádný scénář, akorát psycho zvuky, které dávají filmu ráz.
  <span class="uz">(Anticque, www.csfd.cz)</span></li>
  <li>Pro mně jedním slovem geniální dílko. Tady někde jsou zárodky Lynchovy pozdější tvorby.
  <span class="uz">(Silverluv, www.csfd.cz)</span></li>
  </ul>
