  <p>
  Mladý chlapec, týraný vlastním otcem a matkou jednoho dne zasadí zvláštní semena, ze kterých vyroste jeho babička. U ní 
  posléze začne hledat lásku a útěchu, kterou nenachází u svých rodičů. 
