  <h2 id="zal1">Obsah</h2>
  <p>
  Divácky neúspěšný, ale obrazově opulentní vysokorozpočtový spektákl neilustruje slavný sci–fi román Franka 
  Herberta, ale nabízí autorskou verzi fantaskní a mocné kosmické civilizace zachvácené intrikami, mocenskými 
  půtkami rodové šlechty o podivuhodnou drogu a mesiášskými komplexy. Lynch svedl úporný a s odstupem času ve 
  výsledku čestný zápas s košatou literární předlohou a pragmatickým producentem Dinem De Laurentiisem. V roli 
  vévodského syna Paula Atreida se objevil tehdy čtyřiadvacetiletý Kyle MacLachlan, po jistou dobu Lynchovo 
  alter ego ve filmech Modrý samet a Twin Peaks a v seriálu Městečko Twin Peaks.
  </p>
