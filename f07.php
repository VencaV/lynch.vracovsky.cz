  <h2 id="zal2">Z diskuzí...</h2>
  <ul>
  <li>Ani velká délka filmu nepomohla odvyprávět příběh se všemi důležitými okolnostmi a klíčovými situacemi tak, aby působil 
  jednotně a nezkratkovitě. Některé charaktery jsou zobrazeny až příliš povrchně a některé souvislosti, ačkoli jsou zobrazeny, 
  zůstávají divakovi hádankou i po shlédnutí filmu, jelikož ve filmu už nezbyl prostor pro jejich osvětlení. Pravděpodobně se 
  předpokládá znalost literární předlohy. Vizuálně výtečná podívaná, především interiéry a kostýmy jsou skvělé. Zvuk je dobry, 
  efekty doprovázející řeč některých postav jsou výtečné, skvěle dotvářejí atmosféru. Hudbu jsem kromě uvodních titulků vůbec 
  nepostřehl.
  <span class="uz">(tekket, www.fdb.cz)</span></li>
  <li>Duna od Davida Lynche mě v mých 7 letech formovala takže na ni nedám dopustit, znalci F.Herberta ocení. 
  Jiní asi zavrhnou...
  <span class="uz">(O.Roy, www.fdb.cz)</span></li>
  <li>Dino de Laurentise muselo klepnout, když viděl, do čeho vrazil ty svoje miliony. Lynchova verze je těžko stravitelná, 
  utahaná a depresivní, devadesát procent času člověk kouká na potemělý obraz. Navíc triky mnohdy vypadají, jako by byly 
  stvořeny někdy v 50.letech, v tomhle ohledu hraje prim zejména scéna jízdy na červovi. Ale jsou tu i skvělé momenty. 
  Vynikající je scéna, kdy Muad´Dib pozře živou vodu, tady Lynch naplno rozehrál svoji obrazovou fantazii. Kdyby byl takový 
  celý film, neváhal bych s nejvyšším hodnocením. Jinak Lynch hrubě nezvládá bojové scény. Ty vypadají, jako kdyby vypadly z 
  nějakého parodického kusu bratří Zuckerů. Absolutorium zasluhuje pouze Brian Eno za působivý ústřední hudební motiv Prophecy 
  Theme.
  <span class="uz">(Lima, www.csfd.cz)</span></li>
  <li>Sci-Fi a David Lynch - to proste k sobe nejde a tak neni divu, ze film dopadl, jak dopadl - spatne.
  <span class="uz">(gouryella, www.csfd.cz)</span></li>
  <li>Lynchův styl se pro tak ojedinělou Sci-Fi jakou Duna je, hodí více než dobře. Každopádně díky Herbertově umu budete mít 
  pocit, že toho <b>spousta</b> chybí.
  <span class="uz">(Gemini, www.csfd.cz)</span></li>
  <li>Film se sice drží knižní předlohy jak jen to jde, zachytit ale Herbertem mistrně propracovaný psychologický aspekt veškerého 
  dění se mu však v žádném případě nepodařilo. Vzpomínám si, jak jsem odcházel z kina zklamán, kamže se vlastně ztratila 
  skutečná symbolika vody na Arrakisu.
  <span class="uz">(Jalu, www.csfd.cz)</span></li>
  </ul>
